			<div class="entry-content">
				<div class="page-content">
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
On July 1st, 2017, the Indian Government implemented the Goods and Services Tax (GST) to simplify the country's tax system. The fundamental goal of putting it in place was to have one nation, one tax, and one market. GST has replaced a slew of central and state taxes including excise, VAT, and service tax.
<br /><br />
Before its implementation, every business and service provider registered with the state VAT Law were allotted unique TIN numbers. With the introduction of GST, all taxpayers were unified and brought under the same platform and authority. The need for multiple identification numbers was replaced with a single GST identification number.
<br /><br />
Essentially, the GSTIN stands for the Goods and Services Tax Identification Number. It is a 15-digit alphanumeric code issued to all GST taxpayers and holds key significance in identifying the taxpayer as per the GST ACT.
<br /><br />
That being said, let’s delve deeper into understanding GSTIN.
<o:p></o:p></span></p>


<h2>1.	The Structure of GSTIN</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
As previously mentioned, the Goods and Service Tax Identification Number is a unique, alphanumeric code that is assigned to each entity registered for GST.
<o:p></o:p></span></p>


<h3>●	First Two Digits -</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
The first two digits of the GSTIN, denote the state codes as per the 2011 Indian Census. Each of the 28 states of the Indian subcontinent has a unique code assigned to themselves.
<o:p></o:p></span></p>

<h3>●	Next 10 Digits -</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
The next 10 digits of the GSTIN number, which is the 3rd to the 12 digits of the number denote the PAN (Permanent Account Number) of the registered person or company.
<o:p></o:p></span></p>

<h3>●	The 13th Number -</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
The next number is assigned to the registered person or entity based on the number of registrations in the state of registration. Since it is an alphanumeric code, the first 9 registrations are 1 to 9 after which alphabets are assigned.
<o:p></o:p></span></p>


<h3>●	The 14th Digit -</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
The 14th digit remains ‘Z’ by default.
<o:p></o:p></span></p>

<h3>●	The Last Digit -</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
The final digit of the GSTIN is often called a check code, which is used to detect errors. The code may be either numeric or alphabetical.
<o:p></o:p></span></p>

<h2>2.	How to obtain a GSTIN?</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
The GST registration procedure includes obtaining a GSTIN. After the application is approved by the GST Officer, a GSTIN is allocated to the taxpayer.<br />
You can register yourself for GST in two ways, either by the GST Online Portal or via GST Seva Kendra by the Government of India. <br /><br />
In order to apply for GST registration, you will require the following documents:<br />
1.	PAN Card of the corporation (in case of a company)<br />
2.	Corporate Affairs Ministry Certificate of incorporation/ Proof of Corporate Structure Articles of Association<br />
3.	PAN card or Aadhar Card of the authorised signatory<br />
4.	Photographs of all partners of a company and their authorised signatures<br />
5.	Bank account details<br />
6.	Address proof of the business
<o:p></o:p></span></p>


<h2>3.	How to verify and check GSTIN/UIN?</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
Your GSTIN is included on the GST certificate that a taxpayer obtains after registering under the GST Act. In order to validate your GSTIN, you will need to enter it into the government portal. All information about GSTIN is available to the public and hence, it can be verified through the government portal.<br />
Another way to check your GSTIN is by entering your PAN number into the government’s website.<br />
The simplest way to verify the GSTIN is by checking the 3rd to the 12th digits of the GSTIN and cross-checking if they are the same as the PAN.
<o:p></o:p></span></p>


<h2>4.	How to check GST registration status?</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
 A GST Registration Application is processed after 15 days of application but you can check the GST registration status online on the GST portal.
 <o:p></o:p></span></p>

 <h2>5. Advantages of GSTIN</h2>
 <p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
Procuring a GSTIN allows the taxpayer to avail various benefits:<br />
1.	It allows you to get legal recognition as a supplier of goods or services<br />
2.	It allows you to claim Input Tax Credit under the GST IC provision<br />
3.	It enables you to legally collect tax (GST) from buyers<br />
4.	It ensures proper payment and accounting of GST to authorities
<br /><br />
GST registration is required for businesses to take advantage of the many benefits offered under the GST regime. The government has made GSTIN essential for enterprises with an annual turnover of more than Rs. 20 Lakh. If you are a registered dealer, you must file GST returns and pay GST if you have an obligation. In addition, if the GST you paid exceeds the GST due, you can obtain a refund using your GSTIN.
<o:p></o:p></span></p>

<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Goods and Services Tax Identification Number is the need of the hour for every business in India, hence all businesses should apply for one. This will assist them in not only obtaining the appropriate input tax credit but also in maintaining their market reputation. A GSTIN is also useful when applying for a loan to support your business. Various financial companies such as Hiranandani Financial Services offer business loans to help companies acquire merchandise, develop commercial space, and expand their operations. To be eligible, your company must be registered with the Government of India and have a valid GSTIN.
