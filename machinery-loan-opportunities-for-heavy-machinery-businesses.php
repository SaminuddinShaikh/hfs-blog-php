                loans:<br /><br />
                ● Loans up to 50 lakh rupees are available.<br />
                ● The loan will cover almost 80% of the equipment’s cost while 20% will be borne by the business.<br />
                ● Repayment tenures range from 1 to 3 years.<br />
                ● The application process is generally simple. Visit the official website of any lender offering machinery loans and upload
                all necessary documents to apply for the required scheme.<br />
                ● Once you have submitted all documents, the lender begins the verification process. Within 72 hours of a loan application,
                the loan amount will be disbursed to your bank account considering the information provided is accurate.<br />
                ● Land registration documents<br />
