                maintenance and repair costs by purchasing new and refurbished machinery which leads to improvement in overall productivity.
                With technological advancements, modern machinery involves less money spent on repairs. As a result, it becomes affordable
                for businesses to own such modern machinery and equipment. The loans are collateral-free which means they can be used to
                purchase new equipment. This makes it possible for small and medium-sized businesses to finance machinery and equipment
                purchases.
              </span>
            </p>

