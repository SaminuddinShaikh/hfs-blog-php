				<div class="page-content">
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">The Micro, Small and Medium business enterprises (MSME) contributes substantially to the GDP of India, often as much as a whopping 40%. It is considered the small scale business sector and it plays a pivotal role in generating employment opportunities in addition to several other benefits. Such businesses fall into both, the organised as well as the unorganized sectors. As lately as in 2016-2017, the MSME Ministry informed the rest of the country that there were an estimated 63 million functioning MSME’s running across various different sectors. Such a high number can only be one’s guess as to how much revenue and employment opportunities were generated all over the country. Despite the herculean leverage that these businesses have in India, they often face constraints, the main one being a stiff competition from privately-funded enterprises. This in turn affects the functioning of such micro, small and medium businesses, affecting the economic development of the country as a whole. This is the main reason why the government of India (GoI) has launched multiple loan schemes and other facilities so that the MSME’s can fund their operations, buy goods, business expansions, employ skilled personnel and many more. 
 <br />
 <br />
<b> Government Loan Schemes for MSMEs’ </b>
There are many lucrative loans and schemes offered by the government of India in lieu of aiding MSMEs’ (micro, small and medium enterprises). The most significant and popular ones of the lot are as stated below;
<o:p></o:p></span></p>

<h2><a href="https://applynow.hfs.in/" target="_blank">APPLY FOR BUSINESS LOAN IN INDIA</a></h2>

<h2>1) MSME Government Business Loan Schemes 59 minutes</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Launched by the government of India in 2018, this scheme was introduced as a working capital loan, which means that all finance offered in this loan was to boost monetary assistance to overall boost the growth of India. Under this scheme, MSMEs’ can get a loan sanctioned up to Rs. 1 crore in a matter of only 59 minutes! The interest rate on this loan is at 8%, making repayment easy for borrowers. Both, new as well as existing businesses can apply for this loan. The loan once approved can take anywhere between 8-12 days for the process to be complete. While the interest rate starts at 8%, the amount permitted under this loan varies from Rs. 1 – 5 lakhs. 
<br /><br />
<b>Requirements for Applying for this Loan</b> <br />
The following mentioned criteria will be needed for a successful application to this loan;<br />
•	Know your customer (KYC) details <br />
•	Documentation related to ownership<br />
•	IT (Income Tax) papers <br />
•	GST (goods and service tax) verifications<br />
•	Bank account statements (recent most 6 months)<br />

 <o:p></o:p></span></p>


<h2>2) Mudra Loans</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Sanctioned by the Micro-Units Development and Refinance Agency organisation (MUDRA), these are possibly the most popular types of loans offered by the government of India towards providing finance to MSMEs’. Under this scheme, micro, small and medium enterprises are given financing through a low cost credit. The loan is financed via private and public sector banks. Following the theme of ‘funding the unfunded’, all the banks in the country offer <a href="https://www.hfs.in/blog/pmmy-mudra-loan-a-boon-to-small-businesses.php"target="_blank">MUDRA loans</a> to all such companies that are working in the trading, manufacturing and service sectors in India. These loans are sub-categorised into three options, namely;<br />
•	<b>Shishu</b> - Loan up to an amount of Rs. 50,000 is sanctioned and the interest rate lies between 10% - 12%. <br />
•	<b>Kishor</b> – Loan amount between Rs. 50,000 – Rs. 5 lakhs is sanctioned and the interest rate lies between 14% - 17% <br />
•	<b>Tarun</b> – Loan amount between Rs, 5 lakhs – Rs. 10 lakhs is sanctioned and the interest rate is a flat 16%<br /><br />
MUDRA offers loans in two forms, namely;<br />
-	<b>Micro-credit schemes (MSC)</b> – Under this type, business loans of up to Rs. 1 lakh are offered to small business enterprises through micro finance institutions. Self-help groups, individuals looking to develop micro enterprises, people promoting small businesses and joint liability groups fall within the category of applying for this type of loan. <br />
-	<b>Refinance Schemes for NBFC’s and Banks</b> – Commercial banks, regional rural banks, non-banking financial institutions and small finance banks fall into the different categories of financial institutions that can avail refinance schemes through MUDRA loans. This only applies if they are financing MSMEs’ via working capital loans of up to Rs. 10 lakhs or business loans.
<o:p></o:p></span></p>

<h2>Eligibility Criteria for MUDRA Loans</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
Business that can apply for MUDRA loans are;<br />
•	Partnership firms <br />
•	Private Ltd. companies<br />
•	Proprietary concern companies<br /> 
•	Public companies <br />
•	Other legal sectors

 <o:p></o:p></span></p>


<h3>3) Credit Guaranteed Fund Schemes for Micro and Small Enterprises (CGTMSE)</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><i style="mso-bidi-font-style: normal;"> </i>This scheme was introduced by the government of India on 30th August 2000. In this type of loan funding, the borrower can avail financial aid without the need for collateral against it. This option is only for those businesses that fall under the MSME category. Applicable to both, existing as well as new enterprises, the credit guarantee fund trust was established by Ministry of MSMEs’ and Small industries. Since May 2016, around 133 establishments have been registered with the credit guarantee fund schemes for micro and small enterprises which consist of public sector banks, 73 regional rural banks (RRB’s), 4 foreign banks, 26 public sector banks and other similar institutions. Female entrepreneurs have more benefit then male entrepreneurs under this scheme and working capital loans up to Rs. 1 crore are provided under this scheme. No collateral and no third party guarantee makes this a lucrative financial aid for those who are falling short of them for the purposes of growing and running their businesses. <br /><br />
<b>Eligibility Criteria for Credit Guaranteed Fund Schemes for Micro and Small Enterprises </b><br />
<a href="https://www.hfs.in/blog/why-are-msme-loans-an-ideal-choice-for-small-business-owners.php"target="_blank">MSME’s</a> that are eligible to apply for this loan are;<br />
•	Companies into manufacturing <br />
•	Companies into retail trading <br />
•	Educational institutions <br />
•	Training institutions <br />
•	Self-help groups <br />
•	Businesses in the service sector
 <o:p></o:p></span></p>

<h3>4) Stand-Up India Scheme</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">The Stand-Up India scheme has been introduced by the government of India to provide loans to businesses that are owned and/or run by people from scheduled or backward classes and women. As per the MSME Ministry, around 20% of MSMEs’ in India are run by females and around 66% are run by men and women from scheduled casts or social backward groups. To ensure that such people, who are just as diligent and hardworking, do not suffer from lack of financial aid, giving every cast, creed and gender and fair play at running their businesses. The stand-up India scheme was introduced in 2016 and gives a bank loan ranging from Rs. 10 lakhs – Rs. 1 crore for backward classes and women who are starting new ventures which are excluding the farming sector. This is a collateral-free loan and can be applied for at banks under the Credit guaranteed Fund Scheme with a capped interest rate. This loan can be repaid in seven years with a moratorium period of a maximum of eighteen months.
<o:p></o:p></span></p>

<h3>Eligibility Criteria for Stand-Up India Scheme</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
The MSME’s that can apply for the Stand-Up India Scheme are;<br />
•	Companies into manufacturing <br />
•	Enterprises into trading <br />
•	Service based sectors <br /><br />
It is imperative to note here that in case the business if not of an individual undertaking, a minimum of 51% of the shares of that company should be owned/possessed by the individual who is a female and/or who is from the Scheduled Tribes or Schedules Cast category.
<o:p></o:p></span></p>


<h3>5) Udyogini Scheme</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">The agenda behind this scheme is quite clear, owing to its name – Udyogini, which means women empowerment. This scheme was launched by the Women Development Corporation to encourage women empowerment in India and the funding under this is for those women who need support in meeting capital requirements for starting and running their businesses. A female entrepreneur should be within the age range of 18 years to 55 years to be able to apply for this loan and the maximum amount that is granted for this loan is Rs. 15 lakhs. The yearly income for the said female’s family should be no more than Rs. 15 lakhs for her to be able to eligible. This is a collateral-free loan and no processing fees or other costs are incurred for the borrower. For those who might be widows or handicapped have no limit for income under the Udyogini scheme. This is a great scheme for all women who wish to start a lucrative business are are struggling for funds. 
<br /><br />
<b>Eligibility Criteria & Documents Required for the Udyogini Scheme</b> <br />
The following eligibility criteria will be required for women applying for the Udyogini scheme. <br />
•	Age between 18 years – 55 years <br />
•	Family income being no more than Rs. 15 lakhs annually <br />
•	Poverty line card must be submitted <br />
•	Birth certificate <br />
•	Passport-sized photos<br />
•	Aadhar card <br />
•	Caste certificate <br />
•	Passbook <br />
•	Ration card <br />
•	Income proof 
<o:p></o:p></span></p>

<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">All the above-mentioned schemes display a complete commitment and drive towards building Micro, small and medium businesses all over the country. Many are collateral-free, offer low interest rates and have high amounts being granted. With such government approved offers, no one should worry about the financial aid for starting or running their businesses. This works as a win-win situation as the business owner gets sufficient money to cover expenses while the success of their business adds to the overall GDP of the country.
