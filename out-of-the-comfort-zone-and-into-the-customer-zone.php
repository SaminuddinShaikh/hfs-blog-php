<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">The prolonged quarantine of 2020 has not only affected social circles, but customer-seller relationships may also have taken a hit. This has called for many businessmen to find ways and means, to regain/retain their old customers. <br><br>We have gathered a few tips for you to do just that. Follow the simple ideas that have been listed below and you won’t be worrying about your customers forgetting you any time soon. <o:p></o:p></span></p>


<h2>1)	Plan a Social Announcement (online) for when you resume operations</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">While most businesses have already resumed with necessary precautions, a resumption of services isn’t useful until your customers know about it. Plan a social media announcement or send out an email/SMS/Whatsapp to your customers to let them know that you’re ready to welcome them back. If you don’t have a database of contact details, social media posts can help you cope with this without any kind of trouble. A pro tip could also be to change your cover picture on your Facebook page so whenever someone is directed to your page, they know that you’re open for business again!<o:p></o:p></span></p>



<h2>2)	Update your ‘Google My Business’ listing with new operation hours</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">With the current situation across the globe, you don’t need us to tell you that it is unsafe to reboot your business and go back to functioning like how you did before the lockdown. This means that you can’t be functional full-time. Hence, a smart way around this could be by updating your operation hours on your ‘Google My Business’ listing.<o:p></o:p></span></p>



<h3>3)	Sell the content/products that matter</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><i style="mso-bidi-font-style: normal;"> </i>One needs to be sensitive through this time to help his/her community. If you have a general store or if your business revolves around FMCG products, add masks, gloves, sanitizers, etc. in your product listings. Let your customers know that you care about them and their safety as much as yours.<o:p></o:p></span></p>



<h3>4)	Listen to Customer Feedback</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">While we already covered how customer feedback plays an important role in determining the future of a business, one can’t deny the role that feedback plays now, especially after the long quarantine gap. Listen to your customers and always accept their feedback constructively. The next best idea to grow your business might just be coming from your customer!<o:p></o:p></span></p>

<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Always remember that businesses that evolve with time are the ones that remain intact in the long run. Adapting to times as they change is one of the most vital characteristics of any business.  <o:p></o:p></span></p>				</div>
