<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">The COVID19 Pandemic has caught the entire world, including India, in a certain fix. The largest lockdown in history has held millions of Indians resorting to the close quarters of their homes by giving up their usual routines. <br><br>However, despite the difficulties and the challenges that the current lockdown brings forth, the primary concern is to provide essentials to those who need them. This concern has been met by some <a href="https://www.hfs.in/business-expansion"target="_blank" rel="noopener">small businesses</a> that have taken the foreground on providing necessities to people as and when needed.<br><br>
If you remember your day as it used to be, these businesses are no strangers to your routine, rather, they have always been your support and still continue to be so. Here are a few of them that have shown us hope despite odds, by continuing to spread smiles during this time: 
 <o:p></o:p></span></p>


<h2>1)	Vegetable Vendors</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">-	Due to the lockdown, our accessibility to food deliveries has become limited. However, to tackle this, vegetable vendors have helped form the base of our daily needs by keeping on at their job. In times of such need, they have formed the base of our daily supply of food. One can indeed call them the silent heroes that have supported us in making our quarantine much easier by heeding to our routine needs by going to work themselves. <o:p></o:p></span></p>
<p class="MsoNormal" style="line-height: 150%;"><a name="_heading=h.gjdgxs"></a></p>


<h2>2)	Kirana Stores</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">-	Your friendly neighbourhood general stores have proven to be a go-to stop for all your essentials including the ingredients you need, to be able to cook the recipes you’re currently learning, and also miscellaneous items like dishwashing soaps, scrubs, etc.  From helping solve your quarantine snack urges, to making sure that you receive your stock of grains and vegetables to last a few days, General Stores have made getting through quarantine, much easier for all of us!<o:p></o:p></span><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><o:p>&nbsp;</o:p></span></p>


<h2>3)	Transportation Business</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">-	It is one thing to avoid travelling due to safety during a lockdown but it also is another thing to travel so that the supplies of daily essentials including vegetables, ingredients, snacks, etc., reach their respective distributors so that the stock doesn’t fall short. Transportation Services have been playing an active role by delivering essentials in their lorries and mini-vans which now seem to deliver hope as well. They have contributed to the ease of many in this time of need!<o:p></o:p></span></p>


<h2>4)	Pharmacies</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">-	It is indeed true that pharmacies are like clinics for minor health discomforts. May it be a headache or common cold, pharmacies have always provided to be our go-to for health essentials. They have been one of the small businesses that have had our backs in keeping us safe and healthy in quarantine!<o:p></o:p></span></p>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">These businesses, though small,  have made quarantine easier for all of us by becoming our hope. They have formed the foundations of normalcy in a time where normalcy seems absurd. They remind us constantly that good times still exist. They have proved to us that despite being the ‘Micro’ and ‘Small’, they have the <a href="https://www.hfs.in/business-expansion" target="_blank" rel="noopener">capability</a> of supporting many households at once.<o:p></o:p></span></p>				</div>
