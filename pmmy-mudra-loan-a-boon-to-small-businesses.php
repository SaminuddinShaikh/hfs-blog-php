				<h1 class="entry-title title-font text-italic">
					PMMY: Mudra Loan - A Boon to Small Businesses</h1>
							</section>
						<div class="entry-media post-thumbnail post-thumbnail-singular">
				<img src="images/Blog39MainPic.jpg" alt="How to manage loan repayment">
			</div>
			
			<div class="entry-content">
				<div class="page-content">
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">MSMEs are increasing at a considerable speed in India and financing them can become a challenge. Access to lack of funding for buying assets and working capital can become not only costly but also causes hindrance to business growth.
<br />
<br />
Pradhan Mantri Mudra Yojana (PMMY) is a flagship Government scheme to ‘fund the unfunded’. The aim is to bring small businesses, entrepreneurs, micro-units and other trade, manufacturing and service sector units to formal financing and providing them with affordable credit. It was launched by the Hon’ble Prime Minister on 8th April, 2015. The scheme allows small borrowers to avail loans from all Public Sector Banks such as PSU Banks, Foreign Banks, Micro Finance Institution (MFI), and Non-Banking Finance Companies (NBFCs) for loans up to Rs 10 Lakh for non-farm income-generating activities. 
<br />
<br />
MUDRA stands for Micro Units Development and Refinance Agency. The target audience of MUDRA is MSMEs and SMEs. It's divided into three categories called Shishu, Kishor, and Tarun. These loans do not ask for security as they are collateral-free loans provided by NBFCs and banks with the tenure flexible from 36 months to 5 years. They also allow flexible EMIs that suit the needs of the individuals. With the announcement of the scheme, they also announced an agency called MUDRA Ltd. that was created by the Government to monitor and oversee PMMY. <br />
<br />
The three categories, i.e., Shishu, Kishor, and Tarun are used to signify the level of growth/development and funding needs of the micro, small, or medium enterprises. They also offer a reference point for the next phase of graduation and growth they can look forward to. It is ensured that 60% of the credit flows to the Shishu category, leaving the balance to Kishor and Tarun categories depending on the need. 
<br />
<br />
Detailed understanding of the products that are under the Mudra Loan - 
<o:p></o:p></span></p>

<h2>1) Shishu Loan -</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">This loan is known as Mudra Shishu Loan Scheme. It provides loans up to Rs. 50,000 to those businesses who are in the starting phase or are looking to start a business. For this loan, they have to provide quotations and details of the purchased machinery or any other items. These details also have to be shared with the provider/supplier of the machinery. 
<o:p></o:p></span></p>

<h2>2) Kishor Loan -</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Under the Kishor Loan Scheme, a loan of amount Rs. 50,000 to Rs. 5 Lakh is offered to businesses who are looking for additional funds to buy raw materials or machinery for their existing business or expansion. They have to provide an account statement of the last six months, a balance sheet of the last 2 years, an estimated balance sheet for a year or the duration of the loan, income/sales tax returns, sales made before filing the loan, memorandum and articles of association if there any. The businesses also have to provide a report containing the technical and economic viability of the business. 
<o:p></o:p></span></p>

<h2>3) Tarun Loan -</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><i style="mso-bidi-font-style: normal;"> </i>The third type of loan category is called Tarun loan. This loan is for businesses that are already established. Here, a business can avail of a loan from Rs. 500,001 to Rs. 10 Lakh. For this type of loan, the lender has to provide an account statement of the last 6 months, a balance sheet of the last 2 years, an estimated balance sheet for a year or the duration of the loan, income/sales tax returns, sales made before filing the loan, memorandum and articles of association if there any, certificate of SC, ST, OBC, etc., address proof, and identity proof.<o:p></o:p></span></p>

<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
<b>What are the sectors that are covered under this scheme?</b>
<br /><br />
 To meet the requirements of different businesses and their products, the scheme has mentioned certain activities/sectors that the scheme would offer loans to. They include - 
 </span></p>

<h3>1. Food product sector</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><i style="mso-bidi-font-style: normal;"> </i>This scheme would cater to the food sector which includes activities such as achaar/jam/jelly making, papad making, agricultural produce preservation at the rural level, small food stalls, catering/canteen service, cold storage, sweet shops, ice-making units, biscuits, bread and bun making, ice cream making, etc.<o:p></o:p></span></p>


<h3>2. Everyday social and personal service</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><i style="mso-bidi-font-style: normal;"> </i>Loans are offered to gyms, boutiques, tailoring shops, dry cleaning, salons, beauty parlour, medical shops, courier agents, photocopying, etc.<o:p></o:p></span></p>

<h3>3. Textile industry</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><i style="mso-bidi-font-style: normal;"> </i>Since support for textile activities such as handloom, power loom, zari work, and traditional embroidery is underrated, these loans assist them in expanding and growing their business.<o:p></o:p></span></p>

<h3>4. Commercial and Personal Vehicles</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><i style="mso-bidi-font-style: normal;"> </i>The loan also caters to those who want to purchase a transport vehicle for goods and other services. It also includes personal transportation such as auto-rickshaws, three-wheelers, e-rickshaw, passenger cars and taxis, small goods transport vehicles, etc.<o:p></o:p></span></p>
 
 
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><b>What are some of the benefits offered by the MUDRA Loans? </b>
<br /><br />
MUDRA loans cater to traders, shopkeepers, vendors, and MSMEs who are into the trading, manufacturing, and service sector. Their <a href="https://www.hfs.in/business-expansion.php"target="_blank">loans are also without collateral</a>, so businesses can borrow loans without risking any personal or business assets. <br /><br />
This loan can be used to start, grow, and expand your business with funding up to Rs. 10 Lakh depending upon the requirements. The loans are also offered in both urban and rural areas with less interest rates for women. People from SC/ST category can also avail of loans from MUDRA with concessional interest rates.

 <o:p></o:p></span></p>		

