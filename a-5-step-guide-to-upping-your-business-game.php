<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">“I want to start my own business and make it grow” is probably one of the most daring challenges that one can commit to. However, 36 million entrepreneurs across the country are also aspiring to grow just like you!<o:p></o:p></span></p>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">But worry not, for we have a 5 step guide to up your business game: <o:p></o:p></span></p>

<h2>1)	Have a vision and plan</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">A vision defines where you want to be in the future, whereas ‘Planning’ is the forecast of your journey in devising a strategy to achieve your end objective.<span style="mso-spacerun: yes;">&nbsp; </span>A well-thought-of business plan is one that lists your company’s goals and strategies for future growth. So make sure you have a business plan and act as planned. It is only natural that it would lead your way towards your destination.
<o:p></o:p></span></p>

<h2>2)	Get to know your customers</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Customer feedback leads to customer understanding and this keeps your performance in check. Feedback plays an important role in analysing old business strategies and making way for better ones to come. <o:p></o:p></span></p>

<h2>3)	Research the competition</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">A<i style="mso-bidi-font-style: normal;"> </i>good businessman always knows his competition. Knowing your competition helps you understand what is new in the market. This also enables you to make smarter choices in terms of marketing and planning and stay one step ahead of your competition. You can easily know about your competition through social media. It is only simpler today, so why wait?<o:p></o:p></span></p>

<h3>4)	Be welcome towards partnerships and collaborations</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Remember the days when you were told to work in a group for a class project and it was nice to be helped by others in achieving a common goal? Well, that’s exactly what happens in businesses too. Partnerships and collaborations are very helpful and productive when you and your partner can help each other achieve common and individual goals. This also results in growing not just alone, but together!<o:p></o:p></span></p>


<h3>5)	Always aim high(er)</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">The path to growth is never-ending and so, you shouldn’t ever stop pursuing it. Growth is what makes us better versions of ourselves and also our companies and dreams. One must always aim to achieve higher today than what they achieved yesterday. Compete with yourself and don’t let anyone or anything stop you from growing. <o:p></o:p></span></p>

