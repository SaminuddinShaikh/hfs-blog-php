<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
Time and time again, Micro, Small and Medium Enterprises have proven to be the backbone of our economy, blooming even after the pandemic hit them. However, the undeniable effects of the pandemic have been extremely visible on these MSMEs.
<br /><br />
Small business owners struggle with the concept of collaterals and as traditional lending institutions demand collaterals, it is extremely difficult for them to opt for a loan. So, while banks and NBFCs have been trying their best to provide assistance to their growth, the government has released various schemes that will help these MSMEs with a smoother, and a hassle-free process. 
<br /><br />
From a pool of various schemes for MSMEs, the CGTMSE scheme is a highlight! 
So, let us find out how MSMEs can benefit from this scheme.
<o:p></o:p></span></p>

<h2>What is CGTMSE?</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
To specify the previously mentioned point of MSMEs and security, this scheme was created to ease this issue of collaterals. Credit Guarantee Fund Trust for Micro and Small Enterprises (CGTMSE scheme) is an initiative by the Government of India in collaboration with the Ministry of Micro, Small, and Medium Enterprises (MSME) and the Small Industries Development Bank Of India (SIDBI). Launched in 2000, the aim of this scheme is to offer credit guarantees to financial institutions that lend loans to MSMEs. This results in MSMEs being able to avail loans without collateral, and it strengthens the delivery system by facilitating the flow of credit to the MSME sector.
<br /><br />
The credit guarantee available under this scheme is 75% or 85% for the defaulted principal loan amount of Rs. 50 Lakh. The percentage guarantee is 85% for micro-enterprises availing a loan up to Rs. 5 Lakh. The maximum guarantee is 50% for loan amounts greater than Rs. 50 Lakh but under 1 Crore. Moreover, the guarantee amount for repayment is 80% of the loan amount in case the MSME is by a woman or in the location of North East Region including Sikkim, Jammu and Kashmir. The guarantee covered under this scheme is for the agreed tenure of the term loan/composite credit. <br /><br />
However, in the case of working capital loans, the guarantee cover is of 5 years or a block of 5 years. Rehabilitation of business units if the failure is beyond the control of management to the extent of Rs. 1 crore as support to the lender is provided for assistance in resuscitating the enterprise. <br /><br />
The payable fee to the trust under this scheme is a one-time guarantee fee of 1.5% and an annual service fee of 0.75% in the credit facilities sanctioned. For loans that are up to Rs. 5 Lakh, the one-time guarantee fee is 1% and the annual service fee is 0.5% respectively. Further, for loans in the North-East Region, the one-time guarantee fee is only 0.75%. 
<br /><br />
New, as well as existing MSMEs engaged in manufacturing and service activity, can take advantage of this fund. Some exclusions from this scheme are - Retail Trade, Educational Institutions, Agriculture, Self Help Groups, and Training Institutes.
<br /><br />
Which lending institutions are eligible for this scheme? 
<br /><br />
●	Scheduled Commercial Banks, be it public or private banks or foreign banks. <br />
●	NABARD has also classified some Regional Rural Banks eligible under the “Sustainable Viable” category. <br />
●	There are lending institutions registered as Member Lending Institutions (MLIs). They comprise of:<br />
-	Public Sector Banks <br />
-	Private Sector Banks <br />
-	Regional Rural Banks <br />
-	Foreign Banks <br />
-	Delhi Financial Corporation <br />
-	Kerala Financial Corporation<br />
-	Jammu & Kashmir Development Finance Corporation Ltd. <br />
-	National Small Industries Corporation (NSIC), North Eastern Development, Finance Corporation (NEDFI)<br />
-	Small Industries Development Bank of India (SIDBI)
<br /><br />
Every lender imposes a certain cost on the borrower. The majority of the lenders recover the CGTMSE loan interest rate that does not exceed 14% to 18% including the guarantee cover.
<o:p></o:p></span></p>

<h2>How can MSMEs opt for this scheme?</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
-	The first and most important step to avail this scheme is to prepare a proper business plan which includes a detailed financial plan that can explain the viability of the project. <br />
-	Once the business model is final, one can decide on which lending institution to approach regarding the loan. After that, the application and the business model has to be submitted to the bank. <br />
-	The bank will then check and verify the details of the application as well as the model of the project. <br />
-	After this verification, the bank will send the application to the CGTMSE fund, where the application will again be checked properly before getting approved. <br />
-	Only after the application and the business model has been checked and verified by the bank and the CGTMSE fund will the funds be released for the business. <br />
-	After the approval, the business has to pay the borrowers the CGTMSE guarantee and service fee.
<o:p></o:p></span></p>

<h2>What are the documents required for the CGTMSE loan process?</h2>
<h3>Establishment of a Business Entity :</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
The unit has to be incorporated a business enterprise in the appropriate category like proprietorship, partnership, or a limited company and necessary approval, certificates, and tax registrations. 
<o:p></o:p></span></p>

<h3>Business Project Report :</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
 The components of the project must be thorough in market study and analysis. The factors to be mandatorily covered are: Business Model, Promoter Profile, Cost and other Financials.
<o:p></o:p></span></p>

<h3>Obtaining of CGTMSE Loan Cover :</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
After according sanction to the loan, it is the responsibility of the lender to file for guarantee cover at the CGTMSE. 
