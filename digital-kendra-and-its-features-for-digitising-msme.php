				<h1 class="entry-title title-font text-italic">
					Digital Kendra and its features for digitising MSME! </h1>
							</section>
						<div class="entry-media post-thumbnail post-thumbnail-singular">
				<img src="images/Blog32MainPic.jpg" alt="How to manage loan repayment">
			</div>
			
			<div class="entry-content">
				<div class="page-content">
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">MSMEs are the backbone of the country’s socio-economic development as they contribute to at least 30% of the GDP. Digitisation of MSME is extremely important as the Government plans to increase the sector’s contribution to at least 40%. This step will not just give them increased opportunities with a larger business revenue but also create a huge impact on India’s economy. At the same time, this step will also provide about additional five crore career opportunities to this sector!
<o:p></o:p></span></p>

<h2>Why is it important for MSMEs to venture into the zone of digital media?</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">E-commerce provides small businesses to break free of the limitations that physical outlets impose, helping them sell their stock online. This route also provides them with the freedom of analysing competition, measuring the effectiveness of their sales, and also allows them to increase customer engagement. This service of helping MSMEs with understanding the process of e-commerce and training them will ease their business pattern and allow them to expand. 
<o:p></o:p></span></p>

<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">With the growth of modernisation, demand from customers, and competition, it has become incredibly important for small businesses to embrace the new-age digital era. The process of availing a business loan can also become easier through the online process. If MSMEs adopt the latest technology and are taught how to digitise their businesses actively, it may result in benefits such as an increase in business growth parameters, fewer human errors, and even higher work efficiency.
<o:p></o:p></span></p>

<h2>Benefits of ‘Digital Kendra’ to MSMEs in India - </h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">With the effect of the pandemic on small and medium businesses, almost all of them have started to opt for digital media for their sales. To work on the aim of digitising MSMEs, Amazon launched ‘Digital Kendra’, a place where small and medium businesses can learn the importance and benefits of e-commerce, avail third party services like logistics and shipping help, and get assistance with GST and taxation process. 
<br />
This will give MSMEs a kick start to launch their business online and start on the journey to becoming entrepreneurs. 
<o:p></o:p></span></p>

<h2>The various features of Digital Kendra:</h2>

<h3>1.	E-commerce training:</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">E-commerce provides small businesses to break free of the limitations that physical outlets impose, helping them sell their stock online. This route also provides them with the freedom of analysing competition, measuring the effectiveness of their sales, and also allows them to increase customer engagement. This service of helping MSMEs with understanding the process of e-commerce and training them will ease their business pattern and allow them to expand. 
<o:p></o:p></span></p>


<h3>2.	GST and taxation aid:</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">When put into comparison with large sectors, the process of accounting and taxation for small and medium businesses is not as stable and strong. Digital Kendra is providing guidance and support to the MSMEs to streamline this process of GST and tax.

 <o:p></o:p></span></p>


<h3>3.	Digital marketing services:</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><i style="mso-bidi-font-style: normal;"> </i>Digital Kendra is providing MSMEs with the support of an additional revenue stream while playing an important role in creating greater awareness. It will also give them a better reach to advertise their products not just locally but globally as well. This increases the revenue generation of small businesses online even without running their business locally.<o:p></o:p></span></p>

<h3>4.	Shipping and logistics assistance:</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">The centre is providing MSMEs with the opportunity of availing themselves of onboarding services if they are interested in registering as sellers. They also provide a range of third-party services like shipping and logistic support.
 <o:p></o:p></span></p>

 <p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><b>The first brick and mortar ‘Digital Kendra’ in Surat - </b>
<br /><br />
Gujarat is the biggest MSME hub with industries like auto components, textiles, jewellery, gems, and ceramics. 
 
 <o:p></o:p></span></p>
 
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">The world has begun to completely shift its business online. Especially after the post-Covid-19 era, there has been an intense shift from the conventional way of performing business. While the small business owners understand the importance of going digital, there is a lack of digital literacy which has bought a stone in the path of their development. <br /><br />
This step of Digital Kendra has paved a way for small and medium businesses to launch their business online to a global audience. While making a significant impact on the technology, logistics, digital payments, and delivery infrastructure, it has created a breakthrough in the MSME sector. 

 <o:p></o:p></span></p>		

