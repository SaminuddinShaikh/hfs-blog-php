<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Your Credit Score acts as a guarantee of loan repayment for sure, but how does a loan affect your credit score? <o:p></o:p></span></p>

<h2>1)	Inquiry</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">●	When you apply for a loan an inquiry is raised to know your credit score <br>
●	All such inquiries raised, are recorded in your credit report<br> 
●	This may have a positive or a negative impact, depending on your credit history. <br>
Note: Multiple inquiries have a negative impact on your credit score. 
<o:p></o:p></span></p>

<h2>2)	Regular Payment</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">●	Your repayment schedule is recorded by credit bureaus <br>
●	If your EMI is not paid regularly, it has a negative effect on your credit score
 <o:p></o:p></span></p>

<h2>3)	Credibility</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><i style="mso-bidi-font-style: normal;"> </i>●	When your EMI is paid regularly, your credit score increases.<br>
●	A high credit score is equivalent to high financial credibility <br>
●	This helps you receive fast approvals on your business loan applications
<o:p></o:p></span></p>

<h3>4)	Better Interest Rates</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><i style="mso-bidi-font-style: normal;"> </i>●	Your financial credibility determines the RoI for your loan <br>
●	When your credit score is high, your RoI is low and vice-versa 
<o:p></o:p></span></p>

