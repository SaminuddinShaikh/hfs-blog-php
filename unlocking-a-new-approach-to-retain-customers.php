			<div class="entry-content">
				<div class="page-content">
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">With the majority of the country almost completing three months of lockdown, the government is now gradually announcing directions towards unlocking the largest lockdown in World History. Considering how most people have forgotten the sense of routine and normalcy, companies and businesses are now focusing on kick-starting their professional dealings just as before. However, apart from gearing up to resuming activities, one major concern is about how to retain customers when reconnecting post-lockdown. <br><br>
We’ve gathered a few steps that will help you reconnect with your customers by letting them know that you care about them beyond the business as well. 
 <o:p></o:p></span></p>



<h2>1)	Keep it Human</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">-	Look at the post-lockdown world as one with the “Humanity First” approach. General conversation about health and precautionary measures are sure to help. Let them know that you see them as human beings first and then as a customer.<o:p></o:p></span></p>



<h2>2)	Spread Awareness</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">-	Figure out how you could spread awareness within your workplace/shop. The simplest ways to do so, would be by using posters and also through visual media like pictures or videos, providing statistical awareness as well. Find out what suits you best and use it as a means to establish yourself conveying the required humanitarian view, for your community and for your customers/clients. <br><br>We all could use help in being updated about hygiene and precautionary measures to remain safe during this pandemic. <o:p></o:p></span><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><o:p>&nbsp;</o:p></span></p>


<h3>3)	Use Online Communication</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><i style="mso-bidi-font-style: normal;"> </i>-	During the lockdown, a lot of us revisited the era of emails and found our safe resort in social media. Send innovative emailers to customers that have subscribed to your mailing lists/newsletter. You can also create engagement-driven content on your official social media handles to help ease your customers out and to make them interact with you. From asking your customers about the highlights of their quarantine, to making them answer a simple quiz about precautions that one must take to prevent being infected; through online communication, you can do it all!<o:p></o:p></span></p>


<h3>4)	Focus on Customer Feedback</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">-	The lockdown has indeed changed perceptions of many about routine life, hence, hold customer feedback to be the most valuable resource for your business. Understand your customers to know what they need from you. Your audience is your best judge. Use the inputs that you receive, as fuel to running your business better. This would help you guarantee a fruitful customer experience that will not only help you retain your customers, but will also help in providing you with the fresh perspective that you might need.  <o:p></o:p></span></p>

<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">One of the many insights that we all gained through the past few months, is that we need to look out for each other in times of need. The relationships that we make with people during such times remain strong for a long time. Let your customers know that you look forward to knowing them as people first. <o:p></o:p></span></p>				</div>
