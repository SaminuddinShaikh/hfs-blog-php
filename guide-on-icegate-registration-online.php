<h3>Benefits of using the ICEGATE Portal</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
The Customs Department has been significantly more accountable after the implementation of the ICEGATE site. This has also aided the Customs department in reducing the number of complaints received from both importers and exporters regarding consignment delivery.<br /><br />
Other advantages of the ICEGATE portal include:<br />
1)	The website facilitates the electronic filing of import and export declarations.<br />
2)	It has enabled customs to respond to importers and exporters following the assessment of shipping bills and bills of entry.<br />
3)	It will assist exporters and importers in monitoring and tracking the document's online status.<br />
4)	This portal is quite useful for submitting questions and receiving rapid responses.
<o:p></o:p></span></p>

