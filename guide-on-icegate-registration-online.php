Documents required for ICEGATE new registration<br /><br />
Some of these documents must be submitted by the applicants in order to complete the registration process.<br />
1)	Aadhaar card<br />
2)	Voter ID card<br />
3)	Passport	<br />
4)	Driving license<br />
5)	Permit or License	<br />
6)	Order of Commissioner or Authorization Letter	<br />
7)	Authorization to G Card or F Card
