<h2>Documents required by the Urban Local Body (ULB) and Town Vending Committee (TVC) to generate Letter of Recommendation (LoR)</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
•	ULB application form with candidate details<br />
•	Membership details of any of these:<br />
&emsp;1.	National Association of Street Vendors of India (NASVI)<br />
&emsp;2.	National Hawkers Federation (NHF)<br />
&emsp;3.	Self-Employed Women’s Association (SEWA)<br />
•	Possession of any supporting documents that claim the individual’s vending business<br />
•	A complete report of local inquiries conducted by ULB or TVC involving community-based organizations and/ or Self-Help Groups (SHGs)<br />
•	The ULBs have to verify the applicants within 15 days of submission of the application
<o:p></o:p></span></p>


<h3>Eligibility Criteria for PM Svanidhi Yojana</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
Loan under PM Svanidhi Scheme are available for all the street vendors active on and before 24th March 2020.<br /><br />
Here’s a list of scheme eligibility criteria as defined:
 <br /><br />
•	Street vendors recognized by the ULBs and/or the ones possessing the ULB issued Identity card or Certificate of Vending<br />
•	The Vendors who have been recognized in the survey but haven’t received a Certificate of Identity card will be provided with a Provisional Certificate of Vending. ULB's will provide permanent identification cards to these vendors.<br />
•	Further, vendors without the above-mentioned documentation can become eligible by getting a Letter of Recommendation (LoR) issues by the ULB or TVC (Town Vending Committee)<br />
•	The vendors residing and doing business in the surrounding peri-urban or rural areas can be eligible by having the same letter of recommendation by TVC or ULB<br />
•	ULB/TVC Verified vendors who left their area of operation due to Covid-19 pandemic (before or during) will be eligible for the loan after they come back and resume their businesses.<br /><br />
To Know more about PM Svanidhi Yojana - <a href=" https://pmsvanidhi.mohua.gov.in/">https://pmsvanidhi.mohua.gov.in/<a>
<br /><br />
With the schemes wide appeal and to reach the masses, banks have been chosen wisely and specifically. Following Lending Institutions will disburse the loan
<br /><br />
•	Scheduled Commercial Banks<br />
•	Small Finance Banks (SFBs)<br />
•	Regional Rural Banks (RRBs)<br />
•	Cooperative Banks<br />
•	Self-help group Banks (SHG)<br />
•	Micro Finance Institutions (MFIs)<br />
•	Non-Banking Financial Companies (NBFCs)
