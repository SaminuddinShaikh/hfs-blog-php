				<div class="page-content">
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
The Indian economy and the economies of developing countries have achieved prolific growth by supporting micro, small, and medium enterprises. Small businesses are the second largest employment provider in our country, thus helping build the Indian economy.
<br /><br />
Compared to the corporate industries, MSMEs and micro industries operate at a lower capital cost. These businesses accelerate the growth and development of underdeveloped areas by eradicating inequalities and ensuring equal distribution of wealth on a national level.
<o:p></o:p></span></p>


<h2>How are MSMEs increasing in importance?</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
• The state governments are responsible for the advancement of small businesses. However, the central government sustains small businesses by introducing various programs for their assistance.<br />
• To promote Indian youth's entrepreneurial skills, the Ministry of Micro, Small, and Medium Enterprises coordinates the government's efforts and facilitates the entrepreneurship quotient in India.<br />
• With the government's motive to propose 'Vocal For Local,' individual entrepreneurship is given prime importance on a national level to ensure employment opportunities for the locals.<br />
• It follows the theory of building the local population from the grassroots, where they attain a good standard of living by receiving excellent employment opportunities.
<o:p></o:p></span></p>

<h2>How Udyog Aadhar helps small businesses?</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
• Opening up any business comes with immense paperwork, mandatory for registering the business. The Udyog Aadhar system was introduced to streamline the registration process and minimize the amount of paperwork.<br />
• The unwanted paperwork is mitigated and the Udyog Aadhar scheme is directly available to MSMEs. This leads to a greater understanding of the various schemes by the State and Central governments.<br />
• Udyog Aadhar has an interlinked union between Government-run banks and business enterprises. There is no need to apply separately for upcoming limits.<br />
• Udyog Aadhar Memorandum (UAM) has made it easy for many entrepreneurs to do Udyog Aadhar registration online.
<o:p></o:p></span></p>

<h2>Why should you apply?</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
• Availing the Udyog Aadhar card is completely free. After registering for Udyog Aadhar, Udyog Aadhar acknowledgment is generated and sent to the recorded email address containing a unique Udyog Aadhar number.<br />
• Existing enterprises that have submitted Memorandum 1 and 2 in the entrepreneurship category are not required to apply for the Udyog Aadhar memorandum separately. Entrepreneurs can file for a separate UAM number on their own accord.<br />
• No restriction limits the issue of the UAM based on a single Aadhar number.<br />
• There is no requirement to upload a supporting document while filling the Udyog Aadhar application. However, some state governments and allied institutions may request documentary proof while filling the Udyog Aadhar Memorandum.
<o:p></o:p></span></p>

<h2>Eligibility criteria of Udyog Aadhar</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
Udyog Aadhar registration can be availed by the following categories of businesses and enterprises:<br />
•	Hindu undivided family<br />
•	Sole Proprietorship<br />
•	Partnership<br />
•	Private limited companies<br />
•	Limited liability partnerships<br />
•	Association of persons and cooperative societies
<o:p></o:p></span></p>

<h3>Important updates on Udyog Aadhar in 2021</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
•	Entrepreneurs can complete the Udyog Aadhar application by applying online or offline.<br />
•	Udyog Aadhar online registration is done via websites or mobile apps.<br />
•	Offline Udyog Aadhar registration is done by visiting the local Udyog Aadhar office.<br />
•	More than one business can be registered. Udyog Aadhar policy supports multiple industry registrations for a single individual.
<o:p></o:p></span></p>

<h3>Documents required for Udyog Aadhar application:</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
•	Aadhar number: To record the 12 digit UID number<br />
•	Category certificate: To signify the social categorization status, such as backward castes, scheduled caste, scheduled tribes, and general category. <br />
•	The owner has to issue a self-certification document confirming the business' existence. The business owner also needs to disclose details regarding the activities of the business.<br />
•	Financial information linked with the Government of India has to be entered into the application form.<br />
•	Proof of ownership of the business enterprise, details of employment, and all other contingent information related to the successful functioning of the business have to be provided.<br />
•	This process does not involve the payment of registration fees. There are zero charges for Udyog Aadhar registration.<br />
•	After successfully filling in all the required details, the unique UAM number is self-generated. The same unique number is sent to the registered email address necessary for availing the card.
<o:p></o:p></span></p>

<h3>Process Udyog Aadhar Registration</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
Step 1 - To apply for Udyog Aadhar online, visit the official website of Udyog Aadhar :<a href="https://udyamregistration.gov.in/"target="_blank"> https://udyamregistration.gov.in/</a><br />
Step 2 - Input the Aadhar number and fill in the other required information.<br />
Step 3 - After entering all the details, click next, and the website will redirect to a different page.<br />
Step 4 - An option of re-verifying the entered data will be given. The entrepreneur should carefully verify the details and confirm their accuracy, then click the submit button.
<o:p></o:p></span></p>

<h3>Udyog Aadhar certificate</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
•	It is an identification certificate given to MSMEs. The certificate is availed to those who complete successful Udyog Aadhar registration of their business.<br />
•	The business is now officially certified by a unique number printed on the Udyog Aadhar certificate. This certificate compensates entrepreneurs on electricity bills, deducts bank interest rates, and gives top priority in awarding government contracts.<br />
•	Udyog Aadhar memorandum is a single-page affidavit required by the MSME department for business registration.<br />
•	The entrepreneur can apply for multiple Udyog Aadhar numbers on a single Aadhar certificate. <br />
•	After the Udyog Aadhar registration process, download the UAM certificate.
<o:p></o:p></span></p>

<h3>Benefits of Udyog Aadhar registration</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
•	There are multiple benefits of Udyog Aadhar registration, such as easy credits, collateral-free loans, decreased rates of interest, and subsidized rates on infrastructural loans.<br />
•	Getting a Udyog Aadhar number provides global market opportunities for local businesses. Udyog Aadhar number is an assurance given by the government of India that their business will be taken care of at the highest level.<br />
•	Udyog Aadhar is a boon to small business owners as it provides legitimacy to their business operations.<br />
•	Udyog Aadhar number is an initial helping hand from the government to the small business owners.<br />
•	Udyog Aadhar Card enables businesses to avail <a href="https://www.hfs.in/business-expansion.php"target="_blank">unsecured business loans</a>.
<o:p></o:p></span></p>

