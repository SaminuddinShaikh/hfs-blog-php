<h2>What are some important eligibility criteria for unsecured loans?</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
Businesses have to meet certain parameters to avail a <a href="https://www.hfs.in/business-expansion.php">business loan without security<a/>. It may change across different financial institutions. However, this is the list of parameters that usually stay standard across all the lenders while applying for an unsecured business loan. 
<o:p></o:p></span></p>	

<h3>●	Age</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
While the age may vary according to the different lenders, usually, the adequate age limit is from 25 to 65 years old. 
<o:p></o:p></span></p>	

<h3>●	Business performance</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
Your business should be in operation for a minimum of three years. 
<o:p></o:p></span></p>	

<h3>●	Tax return</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
The business should be filing income tax returns of the business for at least the past 1 year. 
<o:p></o:p></span></p>	


<h3>●	Financial statements</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
You should have the financial statement of your business for the previous 24 months and the previous year’s turnover, audited by a CA.
<o:p></o:p></span></p>	

<h3>●	Citizenship</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
The applicant should be a citizen of India and should hold no criminal records. 
<o:p></o:p></span></p>	

<h3>●	Credit score</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
Your credit score should be healthy as it can lead to better chances of loan approval.
<o:p></o:p></span></p>	

<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
Today, a lot of financial institutions provide business loans. However, the MSME sector often requires a boost to grow their business. For this, a loan without security can be helpful as it does not require collateral and has a quicker approval rate.</b>
