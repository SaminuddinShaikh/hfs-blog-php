					<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><img src="https://hfs.in/blog/The-Know-How-Before-Applying-For-a-Business-Loan.php" alt="">&nbsp;</span></p>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">The spread of COVID19 across the globe has resulted in a lockdown across the country as a measure of safety. This has caught most business owners in a financial crunch and dilemma. However, we have listed down a few guidelines that will help reduce this stress: <o:p></o:p></span></p>

<p class="MsoNormal" style="margin-left: .5in; text-indent: -.25in; line-height: 150%; mso-list: l0 level1 lfo4;"><!-- [if !supportLists]--><i style="mso-bidi-font-style: normal;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><span style="mso-list: Ignore;">1)<span style="font: 7.0pt 'Times New Roman';">&nbsp;&nbsp;&nbsp; </span></span></span></i><!--[endif]--><i style="mso-bidi-font-style: normal;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Understand your current financial position<o:p></o:p></span></i></p>
<p class="MsoNormal" style="margin-left: .5in; text-indent: -.25in; line-height: 150%; mso-list: l1 level1 lfo1;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><span style="mso-list: Ignore;"><span style="font: 7.0pt 'Times New Roman';">&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;</span></span></span><!--[endif]--><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Your primary concern should be about being well-informed of your current financial position. Knowing your ability to pay outstanding debts, lease or loan repayments will help you plan better further. While you do this, pay careful attention to new government announcements as they come.<o:p></o:p></span></p>
<p class="MsoNormal" style="line-height: 150%;"><a name="_heading=h.gjdgxs"></a></p>
<p class="MsoNormal" style="margin-left: .5in; text-indent: -.25in; line-height: 150%; mso-list: l0 level1 lfo4;"><!-- [if !supportLists]--><i style="mso-bidi-font-style: normal;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><span style="mso-list: Ignore;">2)<span style="font: 7.0pt 'Times New Roman';">&nbsp;&nbsp;&nbsp; </span></span></span></i><!--[endif]--><i style="mso-bidi-font-style: normal;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Contact your Bank/NBFC and check for government announcements<o:p></o:p></span></i></p>
<p class="MsoNormal" style="margin-left: .5in; text-indent: -.25in; line-height: 150%; mso-list: l5 level1 lfo5;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><span style="mso-list: Ignore;"><span style="font: 7.0pt 'Times New Roman';">&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;</span></span></span><!--[endif]--><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Be in constant contact with your bank/NBFC to keep up-to-date with loan repayment related announcements by the government. Just recently, the RBI announced a moratorium which has allowed all lending agencies to extend the original loan repayment tenure by 90 days. For more details about this, click <a href="https://economictimes.indiatimes.com/wealth/borrow/rbis-loan-emi-moratorium-proposal-for-borrowers-heres-all-you-need-to-know/articleshow/74913557.cms"target="_blank" rel="noopener">here</a>.<o:p></o:p></span><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><o:p>&nbsp;</o:p></span></p>
<p class="MsoNormal" style="margin-left: .5in; text-indent: -.25in; line-height: 150%; mso-list: l0 level1 lfo4;"><!-- [if !supportLists]--><i style="mso-bidi-font-style: normal;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><span style="mso-list: Ignore;">3)<span style="font: 7.0pt 'Times New Roman';">&nbsp;&nbsp;&nbsp; </span></span></span></i><!--[endif]--><i style="mso-bidi-font-style: normal;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Communicate with your stakeholders (Vendors/Customers)<o:p></o:p></span></i></p>
<p class="MsoNormal" style="margin-left: .5in; text-indent: -.25in; line-height: 150%; mso-list: l4 level1 lfo2;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><span style="mso-list: Ignore;"><span style="font: 7.0pt 'Times New Roman';">&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;</span></span></span><!--[endif]--><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><i style="mso-bidi-font-style: normal;"> </i>In a business set-up, it is always advised to have strong communication with your stakeholders. These can be vendors, as well as your customers. Strengthen your communication with them via calls, SMS, emails, social media posts, etc. to keep them well-informed and provide insight into the steps you are currently taking to mitigate any risk.<o:p></o:p></span></p>
<p class="MsoNormal" style="margin-left: .5in; text-indent: -.25in; line-height: 150%; mso-list: l0 level1 lfo4;"><!-- [if !supportLists]--><i style="mso-bidi-font-style: normal;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><span style="mso-list: Ignore;">4)<span style="font: 7.0pt 'Times New Roman';">&nbsp;&nbsp;&nbsp; </span></span></span></i><!--[endif]--><i style="mso-bidi-font-style: normal;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Take care of your employees<o:p></o:p></span></i></p>
<p class="MsoNormal" style="margin-left: .5in; text-indent: -.25in; line-height: 150%; mso-list: l2 level1 lfo3;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><span style="mso-list: Ignore;"><span style="font: 7.0pt 'Times New Roman';">&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;</span></span></span><!--[endif]--><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Working from home is a challenge which comes with its own terms. Be considerate towards your employees and strengthen internal communication via WhatsApp, Skype, Facebook, etc. Ensure timely payment of salaries and benefits as and when possible. Your employees are the fuel that will keep your business running despite odds.<o:p></o:p></span></p>
<p class="MsoNormal" style="margin-left: .5in; text-indent: -.25in; line-height: 150%; mso-list: l0 level1 lfo4;"><!-- [if !supportLists]--><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><span style="mso-list: Ignore;">5)<span style="font: 7.0pt 'Times New Roman';">&nbsp;&nbsp;&nbsp; </span></span></span><!--[endif]--><i style="mso-bidi-font-style: normal;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Post-lockdown planning</span></i><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><o:p></o:p></span></p>
<p class="MsoNormal" style="margin-left: .5in; text-indent: -.25in; line-height: 150%; mso-list: l3 level1 lfo6;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><span style="mso-list: Ignore;"><span style="font: 7.0pt 'Times New Roman';">&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;</span></span></span><!--[endif]--><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Once the lockdown is lifted, be sure to have a strategy ready so you can gear up and keep moving forward without too much trouble. This is a critical time for businesses all over the world but at the same time, it provides the space to think of bigger plans for the longer term.<o:p></o:p></span></p>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Perceive this as a time to grow individually and collectively, with and within your business. Use it wisely and make the most of the time in your hands.<o:p></o:p></span></p>				</div>
