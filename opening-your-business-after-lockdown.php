				<h1 class="entry-title title-font text-italic">
					Opening your business after Lockdown: an Insight</h1>
							</section>
						<div class="entry-media post-thumbnail post-thumbnail-singular">
				<img src="images/Blog6MainPic.jpg" alt="Opening your business after Lockdown: an Insight">
			</div>
			
			<div class="entry-content">
				<div class="page-content">
					<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><img src="https://hfs.in/blog/The-Know-How-Before-Applying-For-a-Business-Loan.php" alt="">&nbsp;</span></p>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">One can not ignore the fact that the year 2020 has been quite an eventful one for the world. The COVID19 pandemic took off in China in December 2019 and later grew as an international concern towards the end of January, 2020. The World Health Organisation identified it as a Pandemic on 11th February, 2020, raising alarms across continents about the deadly virus.<br><br>
The first case of COVID19 in India was in Kerala in the month of February. Since then, this virus has been on a widespread journey across state borders causing the entire nation to go into lockdown to ensure safe quarantine of the citizens and containment of the virus at the same time. When the first 21 day lockdown was not enough, there was a second one to follow, and to nobody’s surprise, a third and fourth lockdown followed suit as well. These four lockdowns made it integral for most of us to stay home, calling out for shutting offices and businesses across the country with an exception for the essentials industry. However, recently the government has introduced relaxations in areas with lower/no threat of the virus identified as ‘green zones’. This has allowed industry-wise businesses to gradually open up, but with revised safety guidelines and compliances. <br><br>
Here is a list of things you should do if/when you get to avail the benefit of the relaxation and head back to work: 
 <o:p></o:p></span></p>


<h2>1)	Planning</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">-	If your business ever lacked planning, now is the time to start. A detailed planning activity is of priority to understand the allocation of shifts per person so as to avoid any kind of crowding. This will also help you understand who is required to come to work and when, while the rest can work from home.<o:p></o:p></span></p>
<p class="MsoNormal" style="line-height: 150%;"><a name="_heading=h.gjdgxs"></a></p>




<h2>2)	Spacing</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">-	Ensure that only 33% to 50% of the office area is occupied to avoid crowding. Following the norms of social distancing will be of great priority for a few months to follow even after the lockdown is lifted.<o:p></o:p></span><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><o:p>&nbsp;</o:p></span></p>


<h2>3)	Introducing more shifts</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><i style="mso-bidi-font-style: normal;"> </i>-	Make a timetable in such a way that no one person has to stay in the workplace for too long a time at a stretch. This would reduce the chances of a plausible infection being caught and spread.<o:p></o:p></span></p>


<h3>4)	Sanitary Measures</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">-	Deep cleaning of the workspace is of high importance to ensure the safety of employees and also of the customers who visit. For the latter, keeping an alcohol-based sanitizer at the door for the customers to use, is advised. <o:p></o:p></span></p>

<h3>5)	Health Guidelines</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">-	A healthy workspace is one where collective health is the result of the individuals’ good hygiene. Make regular hand-wash a part of your routine, rather than treating it as a chore. Ensure that your employees are all safe and healthy by arranging periodical checkups for them. This would not only contain the virus if found, but also ensure positive medical care to those sick. <o:p></o:p></span></p>


<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">One needs to be wary of the fact that resuming business post lockdown is going to be a change in lifestyle; personal, as well as professional. For more information on what you should do as a small business owner, read our blog :<a href="https://www.hfs.in/blog/5_Things_that_a_Small_Business_Owner_Should_Do_During_This_Time" target="_blank" rel="noopener"> COVID19- 5 Things that a small business owner should do.</a> <br><br>One can not merely ignore that this time of solitude has helped us retrospect on quite a few walks of life. It has gifted us a fresher perspective and all in all, a new outlook towards our endeavours; on an individual level, as well as the collective. 
Returning to work post-lockdown, is going to be a rejuvenating experience for all of us, indeed. 
