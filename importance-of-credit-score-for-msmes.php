<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
As a small business owner, you must have thought about expanding your business. For this, one requires a business or an MSME loan. It is important to understand how small business funding works, how credit scores for businesses are calculated, and how they are different from personal credit scores. 
<br /><br />
<b>So, what exactly is a credit score?</b><br /><br />
A credit score is a number that depicts a customer’s creditworthiness. The credit score should be between 300-850, and the higher the score, the better is a borrower’s lending potential. <br /><br />
This credit score is based on a number of things like - the number of open accounts, total levels of debt, repayment history, credit history length, credit mix, and new credit. After the calculation, lenders use this credit score to evaluate the probability of an individual repaying a loan in a timely manner. <br /><br />

This credit score is important as it plays a key role in the lender’s decision of offering you credit. This shows how seriously it affects your financial life. A credit score of 700 or above is considered good and a score above 850 is considered excellent. They generally get the benefit of receiving a lower interest rate, which helps them pay less money. However, people with credit scores below 640 are called as subprime borrowers, and lending institutions often charge interest a rate higher as to compensate themselves for carrying the extra risk.
<br /><br />
There are five ranges of credit scores - <br />
●	Exceptional - 800 to 850 <br />
●	Very good - 740 to 799<br />
●	Good - 670 to 739 <br />
●	Fair - 580 to 669 <br />
●	Poor - 300 to 579
<o:p></o:p></span></p>

<h2>What are some factors that can affect your business credit score?</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
●	Any tax liens or collection amount in the last 7 years <br />
●	Payment history indicating how frequently the payments are made on time <br />
●	The number of years a business has been in operation <br />
●	New business credit lines opened, and amount of business credit lines used in the last six months. These lines include cards, invoice accounts, loans, and leases.<br />
●	Lines of credit for businesses applied in the last 9 months. They are known as credit inquiries. 
<o:p></o:p></span></p>

<h2>So, what is a credit gap in the MSME industry?</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
Everyone does not have access to credit, and it can be due to a number of reasons like having no formal borrowing history, lack of financial inclusion, financial illiteracy, etc. India has more than 63.3 million MSMEs and while the high-end firms receive revenues above ₹250 crores, the lower end firms only have a single-person operations like grocers, vehicle owners, village craftsmen, etc. Some narrative also suggests that only 6-10 million MSMEs have formal access to credit, and this might even be an underestimate. <br /><br />
Something as simple as the formalisation of MSMEs bank balance might just be a major driver of credit growth. Since banks aren’t always ready to provide loans, they might even seek informal sources for their requirements. However, the biggest problem they face isn’t just outright rejection of loans but the long timeline for loan approval. While it is granted that banks cannot offer loans considering the fraud and credit risk, field investigations and investigations with education can play a crucial role. <br /><br />
Other solutions like longer range of tenure, access to wider set of credit options, product options can also be helpful. Unsecured lending is also usually preferred by the MSMEs as most of these small businesses do not have an asset to pledge for collateral. 
<br /><br />
Now, the biggest question is how can one improve their credit score?
<br /><br />
Mentioned below are some effective tips that can help maintain a good credit score for your small business. 
<o:p></o:p></span></p>

<h3>●	Update your business information regularly -</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
It is extremely crucial for a business owner to constantly keep a check on the credit score as the credit bureaus can check any reports. Another important aspect of this is to make sure that the information being updated is correct at all times so your business can reflect a good score during assessment. Information that has to be updated regularly for a business is bank statements, balance sheets, no. of years of operation, size of the company, etc. 
<o:p></o:p></span></p>

<h3>●	Pay your dues on time -</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
Your score is severely dependant on basis of your repayment history. Credit bureaus regularly check your previously made payments to assess your credit behaviour. This includes payments that are paid before the due date, whether they are credit card bills or loan EMIs. Dues cleared before time leaves a positive impact on your score and a default can severely ruin your credit score for a long period of time, which can also be troublesome while trying to get out of it. 
Even if you manage to get out of it, a defaulter forever stays in the system and records. 
<o:p></o:p></span></p>

<h3>●	Don’t close a credit card account -</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
It is always suggested to stop using a credit score account instead of closing it. While it depends on the age and credit card limit, closing an account can seriously hurt your credit score due to the difference in utilisation rate. 
<br /><br />
You can also use tactics like partner with lenders that directly report to credit bureaus, up your credit line, pay your bills on time, and work with best credit repair companies. 
<o:p></o:p></span></p>

<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Your credit score can critically impact the financial health of your MSME business and can save you a lot of money in lifetime. A well-maintained credit score can give you benefits like lower interest rates, longer tenure, paying less for any credit, etc. 

