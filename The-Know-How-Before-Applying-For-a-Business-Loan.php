				<div class="page-content">
					<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><img src="https://www.hfs.in/blog/The-Know-How-Before-Applying-For-a-Business-Loan.php" alt="">&nbsp;</span></p>

<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Starting off a business is not as difficult a task as it used to be anymore and growing it, is no more a challenge as before. Business loans have often proved to be a boon for budding entrepreneurs and start-ups. However, there are a few things that you should know before applying for a business loan: <o:p></o:p></span></p>



<h2>1)	Your credit score matters</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Credit score is a major factor that determines whether your request for a loan would be approved or not. If your business has a good credit score, your chances of getting a loan are higher.<span style="mso-spacerun: yes;">&nbsp; </span>Even first-time borrowers with no credit score can avail a business loan, subject to their previous financial discipline.<o:p></o:p></span></p>


<h2>2)	Your income</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Understanding the flow of your cash represents the health of your business. In simpler words, it determines your affordability and hence your power to pay installments for your loan. Unpaid invoices can hinder your cash flow and impact it to a great extent.<o:p></o:p></span></p>


<h2>3)	The present amount of debt</h2>						
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Your<i style="mso-bidi-font-style: normal;"> </i>Your debt-to-income ratio is examined against your gross monthly income. Lenders mostly expect this ratio to be around 50% or lower. The existence of multiple loans reduces the chances of approval of a new loan. So one must be wary of this fact. <o:p></o:p></span></p>


<h3>4)	Age of the business</h3>	
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">With approximately 20% of businesses failing in the first year itself, most lenders expect a minimum age through which the business has run successfully and sustainably. The age that qualifies you for a business loan ranges roughly from 6 months to 3 years depending on each lender.<o:p></o:p></span></p>


<h3>5)	Collateral</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Collateral acts as security to guarantee the repayment of a loan. These are tangible assets already owned by you which can range from being real estate to priced equipment. Lenders hold this as a pledge for the purpose of borrowing.  Collateral is generally not needed for smaller business loans but this depends completely on the discretion of the lender, after assessing your borrowing history. For larger business loans collaterals is required, which is also called as secured business loans by lenders. <o:p></o:p></span></p>

<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Business loans have paved the way towards successful growth of the entrepreneurial industry in the country and also worldwide. Today, it has branched out into other various products such as <a href="https://www.hfs.in/equipment-and-machinery" target="_blank" rel="noopener">equipment and machinery loans, <a href="https://www.hfs.in/working-capital" target="_blank" rel="noopener">working capital loans</a> and more. With the above know-how’s on your checklist, you’re ready to apply for not just a loan but also for the growth of your business. <o:p></o:p></span></p>				</div>
