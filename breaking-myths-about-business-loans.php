				<h1 class="entry-title title-font text-italic">
					Breaking Myths about Business Loans</h1>
							</section>
						<div class="entry-media post-thumbnail post-thumbnail-singular">
				<img src="images/Blog11MainPic.jpg" alt="Breaking Myths about Business Loans">
