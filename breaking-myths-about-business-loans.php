			<div class="entry-content">
				<div class="page-content">
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">If you have a business that is doing just well in the market, then you must think of ways to grow it so that you can build more revenue and generate more profit. However, the lack of funds is always a huge hurdle and can sometimes keep you from fulfilling the business dream you always had. So the next thing you do is think of ways to increase/raise funds for the business. One of the most common solutions for this is opting for a business loan. <br><br>While there are many myths about how raising a business loan can be cumbersome, we will break these myths to help you make a smart choice.<o:p></o:p></span></p>


<h2>1)	A time-consuming affair</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Most applicants submit a loan application much before the time when they need the funds because they feel that the money might not reach them in time due to a “high turnaround time”. Some business owners even drop the idea of taking a loan because of this and then resort to other means of raising money from friends/business associates.<br><br>This is the biggest myth surrounding loan applications, as loan applications are processed in a very time-bound manner, as long as the applicant <a href="https://www.hfs.in/blog/The-Know-How-Before-Applying-For-a-Business-Loan">submits the required information at one go</a>.<o:p></o:p></span></p>


<h2>2)	No need to check the financial health of your business until you have to apply for a loan</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Being financially healthy is a key factor in preparing your business for any unprepared events. For example, if you have a sudden need for capital and the financial health of your business is poor, it will reflect on your credit worthiness when applying for a loan and you will have to compromise by getting a loan with much higher interest rates. <br><br>The CCR (Company Credit Record) gives MSMEs access to regularly evaluate themselves and monitor financial health. Hence, it is always a good idea to constantly evaluate the financial health of your business regardless of whether you need a loan or not. <o:p></o:p></span></p>


<h2>3)	Once a loan is availed, there isn’t any need to worry about your credit score</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><i style="mso-bidi-font-style: normal;"> </i>Credit-worthiness is an important aspect of availing a loan. Lenders often check the credit history while releasing a loan to any applicant and most times either the loan request is rejected or the amount of the loan is downscaled if the credit history of the applicant is not so good. The CCR can be used as a single document to showcase financial credibility from the application to the repayment of the loan. It even provides possible reasons for a loan rejection along with areas needing improvement.<br><br>
Your payment behaviour is proven by your CCR along with your credit score, giving lenders a clear idea of the past payment behaviour of your company. A good credit record determines a projection of strong future behaviour. <o:p></o:p></span></p>


<h3>4)	Collateral is mandatory</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">It is often scary when one thinks of needing possible collateral for applying for a business loan. However, though some loans do need collateral because the amount required is higher or the repayment tenure required is longer, there are other options such as <a href="https://www.hfs.in/business-expansion#product_feature">smart business loans</a> that are collateral-free, where instead of offering collateral, you are asked to provide a personal guarantee. This is a way of lenders considering it to be your responsibility to pay back what your business owes.<o:p></o:p></span></p>


<h3>5)	Only large amounts can be financed</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Due to the traditional lending behaviours of banks, you might sometimes feel that loans are only meant to be availed for a large amount of money. However, this is a complete misconception especially when micro-loan facilities have been made available. There are always such options available for borrowers who require a small sum as a loan.<o:p></o:p></span></p>


<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Bottom line is, if you require a loan for any reason, there are always different options available to suit your needs. Dreams cannot be fulfilled without increasing your credibility and challenging yourself and a feasible business loan will help you achieve your business dreams by being an optimal financial solution. <o:p></o:p></span></p>				</div>
