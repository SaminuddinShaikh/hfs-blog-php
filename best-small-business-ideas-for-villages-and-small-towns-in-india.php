					Best Small Business Ideas for Villages and Small Towns in India</h1>
							</section>
						<div class="entry-media post-thumbnail post-thumbnail-singular">
				<img src="images/Blog41MainPic.jpg" alt="best small business ideas for villages and smalltowns in India">
