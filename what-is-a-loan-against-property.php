				<div class="page-content">
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">We’ve all heard of ‘LAP’ or ‘Loan Against Property’, a type of secured business loan where one keeps his/her property as collateral. In this article, we’ll be taking you through the various aspects of loans against property.  
<br><br> Loan against property has become popular since these types of loans are known to be cheaper sources of raising money for the borrower and relatively safer sources of giving loan for the lender.  The property remains as collateral until the loan is paid to the lender in full. The property offered can be self-owned land, house, shop, showroom, office or warehouse. 
<br><br>Such loans are issued for various business purposes such as purchasing machinery, for the means of working capital, for undertaking expansion etc. 
<br><br>They are also accompanied by various benefits which we’ve listed out for you:<o:p></o:p></span></p>

<h2>1)	Lower interest rates</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Loan against property is a secured business loan for the borrower and the lender as, if the loan cannot be paid, the lender can claim access to the property. This property can later be auctioned and the money can be regained. Due to this secure nature of the loan, the interest rates are relatively lower.<o:p></o:p></span></p>


<h2>2)	Longer tenure</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">A loan against property has benefits such as tenure of around 10-15 years while personal loans only go up to 3-5 years. Low interest and longer tenure make the selection of loans better as customers can repay according to their suitable instalments. <o:p></o:p></span></p>



<h2>3)	Simple to get</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><i style="mso-bidi-font-style: normal;"> </i>As lenders have lower risk in these secured loans, they are generally more open towards disbursing the loan against property. This makes LAP more accessible, subject to the value of the property offered as collateral. <o:p></o:p></span></p>



<h3>4)	The more the market value, the more the loan</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">In a loan against property, as discussed above, a plot of land owned by the borrower is offered as collateral. However, the loan amount sanctioned is decided with correspondence to the market value of the collateral. Usually, lenders sanction a sum of around 60% to 75% of the market value of the property.<o:p></o:p></span></p>

<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Now with a better understanding of LAP, you probably know that if you have the collateral for it, it’s a safe option indeed.<o:p></o:p></span></p>				</div>
