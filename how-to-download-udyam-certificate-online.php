				<div class="page-content">
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">As per the notification of Micro, Small and Medium Enterprises, Udyam Registration is the new process replacing UDYOG AADHAAR launched by the Ministry of MSME effective from 1st July 2020. 
<br /><br />
Udyam Registration, also called MSME Registration, is a government registration for MSMEs providing them with a unique Identity Number and a recognition certificate, to certify them as micro/small/medium enterprises. The primary motive behind this launch was to provide plenty of benefits to MSME businesses in India.
<br /><br />
The types of enterprises who are eligible for Udyam Registration as per the current notification are:
<br /><br />
<i>Micro enterprises</i>: those businesses that can raise capital of up to Rs. 1 Crore and generate turnovers of up to Rs. 5 crores. <br /><br />
<i>Small enterprises</i>: are those entities that raise funding of up to Rs. 10 Crores and generate turnovers of up to Rs. 50 crores.<br /><br />
<i>Medium enterprises</i>: entities capable of investing up to Rs. 50 crores and generating turnovers of up to Rs. 250 crores.

</span></p>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
To print a Copy or Verify the validity of your Udyam Registration pls visit
<a href="https://udyamregistration.gov.in/PrintUdyamCertificate.aspx"target="_blank">https://udyamregistration.gov.in/PrintUdyamCertificate.aspx</a>
<br /><br />
If you have not registered as MSME yet, you can register by using the following link:
<a href="https://udyamregistration.gov.in/"target="_blank">https://udyamregistration.gov.in/</a>
<br /><br />
<a href="https://www.hfs.in/blog/udyam-registration.php"target="_blank">Udyam registration</a> is free, and MSMEs can access their certificates after completing the formalities using a dynamic QR code.<br /><br />
<b>Documents required for Udyam Registration:</b> Documents required are PAN of the Organization, GST Certificate, Aadhaar Copy and Social category of entrepreneur, Mobile No., E-mail address, Date of Commencement of Business, BANK A/c No and IFSC Code (or Pass Book copy), No of Employees (With Bifurcation of Male and Female), Nature of Business and latest Audited Financial Statements
<br /><br />
You may take the following steps to download the Udyam registration certificate:<br />
Step 1– Click on ‘For new entrepreneurs not registered as MSMEs’ on the official Udyam Registration portal<br />
Step 2– Enter your 12-digit Aadhaar Number and click ‘Validate and Generate OTP.’<br />
Step 3– Enter the OTP that you receive on your registered mobile number.<br />
Step 4– Validate your PAN card by choosing the type of business or organization.<br />
Step 5– Provide the company’s postal address, including the pin code, city, and state.<br />
Step 6– Enter your business email address and phone number.<br /><br />
Once the registration is completed, you can download your <a href="https://www.msmeregistration.org/"target="_blank">MSME certificate online</a> by following these steps.
</span></p>

<h2>1.    MSME Certificate Download by Mobile Number</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
You can get your MSME certificate on your mobile by following these simple steps.<br /><br />
a.      Visit the official Udyam portal.<br />
b.      Click on ‘Print Udyam Certificate.’<br />
c.       Enter the application number, registered phone number, and email address to receive the OTP.<br />
d.      Enter the OTP for validation.
</span></p>
<h2>2.    MSME Certificate Download by PAN Number</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
You can also download your MSME certificate by providing your PAN details.<br /><br />
a.      Visit the official Udyam portal.<br />
b.      Click on ‘Print / Verify’ and ‘Print Udyam Certificate.’<br />
c.       Enter your 16-digit Udyam Registration Number and 10-digit mobile number to login into the platform with OTP.<br />
d.      You will get an OTP on your registered mobile number, which you must enter and click ‘Validate OTP and Print.’<br />
e.      Get a copy of the certificate by selecting ‘Print’ or ‘Print with Annexure.’<br /><br />
You will get the certificate only by choosing ‘Print’ and both the certificate and UAM application by selecting ‘Print with Annexure.’
</span></p>
<h2>How to Download Udyam Certificate Online?</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
You can follow these steps to download your Udyam registration certificate via the official government portal.<br /><br />
Step 1–   Visit the official <a href="https://udyamregistration.gov.in/Government-India/Ministry-MSME-registration.htm"target="_blank">Udyam registration</a> portal.<br />
Step 2– Click on ‘Print Udyam Certificate.’<br />
Step 3– Enter your Udyam application number and registered mobile number or Email address and the OTP that you will receive.<br />
Step 4– Check the status of your Udyam certificate and download and print a copy.
</span></p>

<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
To conclude, Udyam is a platform recognized under the Gazette of India where MSMEs can register themselves for tax and other purposes. So, when you find a start-up, it would be better to register it as an MSME under Udyam, although it is not mandatory yet.
