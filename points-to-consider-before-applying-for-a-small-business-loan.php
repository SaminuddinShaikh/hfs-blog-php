        <div class="entry-content">
          <div class="page-content">
            <p class="MsoNormal" style="line-height: 150%">
              <span
                style="
                  font-size: 14pt;
                  line-height: 150%;
                  font-family: 'Poppins', 'sans-serif';
                  mso-fareast-font-family: Poppins;
                  mso-bidi-font-family: Poppins;
                "
                lang="EN"
                >If you are running a business and are thinking of taking a small business loan to improve your business, there are certain
                factors to be considered before applying for the same.
                <o:p></o:p>
              </span>
            </p>

            <p class="MsoNormal" style="line-height: 150%">
              <span
                style="
                  font-size: 14pt;
                  line-height: 150%;
                  font-family: 'Poppins', 'sans-serif';
                  mso-fareast-font-family: Poppins;
                  mso-bidi-font-family: Poppins;
                "
                lang="EN"
              >
                1. Purpose of your business loan: It is important for you to first decide why you want the loan. You may want to start a
                business, or expand your business, or you may want to improve your cash flows or working capital on a short-term basis, or
                you may want to invest in plant or machinery. If you are clear about the purpose, then you can proceed to look at other
                factors
                <o:p></o:p>
              </span>
            </p>
            <p class="MsoNormal" style="line-height: 150%">
              <span
                style="
                  font-size: 14pt;
                  line-height: 150%;
                  font-family: 'Poppins', 'sans-serif';
                  mso-fareast-font-family: Poppins;
                  mso-bidi-font-family: Poppins;
                "
                lang="EN"
              >
                2. Type of business loan: Many lenders offer
                <a href="https://www.hfs.in/blog/guide-to-improving-odds-of-getting-a-small-business-loan.php" target="_blank"
                  >small business loans.</a
                >
                You need to decide whether you want to offer collateral for your loan or want to apply for a collateral-free (unsecured)
                loan in which case the interest rate would be higher.
                <o:p></o:p>
              </span>
            </p>
            <p class="MsoNormal" style="line-height: 150%">
              <span
                style="
                  font-size: 14pt;
                  line-height: 150%;
                  font-family: 'Poppins', 'sans-serif';
                  mso-fareast-font-family: Poppins;
                  mso-bidi-font-family: Poppins;
                "
                lang="EN"
              >
                3. Nature of the business loan: The tenure of the loan may be short term or long term. Depending on whether you want to
                manage your business expenses to streamline your cash flows, or whether you want to invest in assets, the tenure may vary.
                <o:p></o:p>
              </span>
            </p>
            <p class="MsoNormal" style="line-height: 150%">
              <span
                style="
                  font-size: 14pt;
                  line-height: 150%;
                  font-family: 'Poppins', 'sans-serif';
                  mso-fareast-font-family: Poppins;
                  mso-bidi-font-family: Poppins;
                "
                lang="EN"
              >
                4. Your CIBIL score: The CIBIL score is a measure of your credit worthiness. It is useful to obtain a CIBIL score report
                which is a score between 300 and 900. A score above 700 is generally considered good. The better the CIBIL score the higher
                your chances of your loan application getting approved.
                <o:p></o:p>
              </span>
            </p>
            <p class="MsoNormal" style="line-height: 150%">
              <span
                style="
                  font-size: 14pt;
                  line-height: 150%;
                  font-family: 'Poppins', 'sans-serif';
                  mso-fareast-font-family: Poppins;
                  mso-bidi-font-family: Poppins;
                "
                lang="EN"
              >
                5. Terms and conditions of loans: Different lenders have different terms and conditions for processing loans. Terms related
                to processing fees, interest rate, documentation, time is taken for processing the loan, etc. may vary. It would be useful
                if you can get information on these which will help you to select the right type of lender for your requirement. There are
                also some lenders who can help you customise your loan as per your repayment ability and the tenure and amount of the loan.
                <o:p></o:p>
              </span>
            </p>
            <p class="MsoNormal" style="line-height: 150%">
              <span
                style="
                  font-size: 14pt;
                  line-height: 150%;
                  font-family: 'Poppins', 'sans-serif';
                  mso-fareast-font-family: Poppins;
                  mso-bidi-font-family: Poppins;
                "
                lang="EN"
              >
                6. Facility for online application: Today many lenders offer an online platform for the submission of loan applications and
                promise hassle-free loan processing with minimal processing fees. You would need to get acquainted with the online mode of
                application, and the various terms and conditions involved with respect to the same.
                <o:p></o:p>
              </span>
            </p>
            <p class="MsoNormal" style="line-height: 150%">
              <span
                style="
                  font-size: 14pt;
                  line-height: 150%;
                  font-family: 'Poppins', 'sans-serif';
                  mso-fareast-font-family: Poppins;
                  mso-bidi-font-family: Poppins;
                "
                lang="EN"
              >
                7. Documentation: It is necessary for you to ensure that all the documentation required for submitting to the lender is kept
                ready. Documentation such as identity proofs, address proof, certificate of incorporation of the business, partnership deed
                (if applicable), bank statements, audited financial statements such as profit & loss account and Balance Sheet, or Income
                and Expenditure statement, income tax returns, etc. need to be kept ready both as hard copies as well as scanned copies.
                <br />
                <o:p></o:p>
              </span>
            </p>
            <p class="MsoNormal" style="line-height: 150%">
              <span
                style="
                  font-size: 14pt;
                  line-height: 150%;
                  font-family: 'Poppins', 'sans-serif';
                  mso-fareast-font-family: Poppins;
                  mso-bidi-font-family: Poppins;
                "
                lang="EN"
              >
                To conclude, the factors explained above can help you understand how to get a small business loan. Make sure to consider
                them before you apply for the loan. <a href="https://applynow.hfs.in/" target="_blank">Apply for Business Loan</a>
                <o:p></o:p>
              </span>
            </p>
