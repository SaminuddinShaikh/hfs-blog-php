        <section class="entry-header page-header text-center">
          <h1 class="entry-title title-font text-italic">Points to Consider Before Applying for a Small Business loan</h1>
        </section>
        <div class="entry-media post-thumbnail post-thumbnail-singular">
          <img src="images/Blog94MainPic.jpg" alt="Small Business loan" />
