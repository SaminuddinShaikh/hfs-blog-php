				<h1 class="entry-title title-font text-italic">
					Solve the Challenges of Delayed Payment like a Pro!</h1>
							</section>
						<div class="entry-media post-thumbnail post-thumbnail-singular">
				<img src="images/Blog35MainPic.jpg" alt="How to manage loan repayment">
