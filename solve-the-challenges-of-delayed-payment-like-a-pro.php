			<div class="entry-content">
				<div class="page-content">
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">The development of the MSME sector will have major growth potential for the entire country. However, one of the severe problems hindering this growth is of delayed payment. A lack of timely payment results in MSMEs going into debt, which interrupts their expansion.
<br />
We have curated a list of solutions so you can address this challenge like a pro!
<br />
<o:p></o:p></span></p>


<h2>●	Make a follow up plan -</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">It is recommended to take action as fast as possible after the due date of the invoice. You can follow a schedule of 3 days, one week, and 30 days respectively with email, call, or get in touch with the department head for the follow up. 
<o:p></o:p></span></p>



<h2>●	Implementation of the MSMED act, 2006 -</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">In the interest of helping MSMEs, the MSMED act, 2006 specifies a 45-days’ time period for the payment of any goods and services to the MSME business. If delayed more than that, they also allow them to directly log in cases about delayed payment by Central Ministries / Departments / CPSEs / State Government. 
 <o:p></o:p></span></p>


<h3>●	Effective use of TReDS -</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><i style="mso-bidi-font-style: normal;"> </i>TReDS is an e-platform on which the trade receivables are auctioned. In this system, business owners can sell their bills to a bank or other financial institutions before their due date. This means that the sellers can get credit on their invoices and continue working without interruption! 
<o:p></o:p></span></p>



<h3>●	MSME Samadhaan -</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">MSME Samadhaan, a portal launched in 2017 was made to directly register cases related to delayed payments. Micro and small enterprises who have registered on Udyam Aadhaar are eligible to apply at the portal. These cases are studied and mutually settled between the buyers and the MSMEs.
   <o:p></o:p></span></p>

 
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Delayed payments can vastly affect a small business with issues like the inability to buy new stock, expand the business, or even deal with bigger clients. Bits of advice is easier to give and harder to follow. However, with the correct steps and a good relationship, you can call and follow up and take action instead of waiting.
 <o:p></o:p></span></p>		

