				<h1 class="entry-title title-font text-italic">
					SIDBI Schemes and Loan Offerings for MSMEs – The Complete Guide </h1>
							</section>
						<div class="entry-media post-thumbnail post-thumbnail-singular">
				<img src="images/Blog52MainPic.jpg" alt="Udyog Aadhar">
			</div>
			
			<div class="entry-content">
				<div class="page-content">
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
SIDBI stands for Small Industries and Development Bank of India. It was established in the year 1990 with a vision to enhance cash flow in the MSME sector, promote small scale industries and thus enable faster economic development at a national level. It provides funds and customised loan schemes to small-scale industries for commercializing new-age businesses. The bank extends financial support to MSMEs to meet the growing market demands, ensure cash flow and stabilize operations. The Mission and Vision of SIDBI is to make MSMEs self-sufficient and competitive in the global market apart from helping them adopt latest technologies and processes that can take their business to new heights.
<o:p></o:p></span></p>

<h2>Major Objectives of SIDBI Scheme</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
•	Support the holistic growth MSME Sector<br />
•	Ensure smooth operations with Working Capital loans<br />
•	Provide financial support to expand small-scale businesses
<o:p></o:p></span></p>

<h2>Advantages of SIDBI Loans</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
Business loans offered by SIDBI are customised according to the financial requirements of the enterprise. These flexible loans are given out based on the business size and turnover at attractive rates of interest as it’s associated with major financial institutions such as Japan International Cooperation Agency and World Bank. It also offers necessary financial advice to MSMEs and support in terms of loan processing. Depending on the eligibility criteria, MSMEs can avail an unsecured loan up to Rs. 1 Cr. Financial assistance is offered to ensure the overall growth of the business. SIDBI helps raise funds for MSMEs through equity and venture capital funds as well. The transparent and quick procedure makes SIDBI a trustworthy source of funding for MSMEs in India.
<o:p></o:p></span></p>

<h2>Types of Financial Assistance offered by SIDBI</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
•	For sustainable development to ensure energy efficiency and cleaner production <br />
•	For upgradation or modernisation of business, aid in buying new machinery, setting new branches<br />
•	For Technology Information Forecasting and Assessment Council - TIFAC<br />
•	To improve credit flows and enhance skill development<br />
•	To fund recruitment and talent acquisition for business expansion<br />
•	To raise working capital, ensure stability of operations, restructuring of business, renovation etc.
<o:p></o:p></span></p>

<h2>Other functions of SIDBI</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
•	Ensures financial health of commercial banks and corporations<br />
•	Marketing and advertising initiatives<br />
•	Raise funds through venture capitalists, regularised banks, State Finance Corporations, NBFCs etc. <br />
•	Support the holistic growth of MSMEs across sectors to strengthen the nation’s economy
<o:p></o:p></span></p>


<h2>Direct Loans offered by SIDBI</h2>
<h3>• SPEED -</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
Loan for Purchase of Equipment for Enterprise’s Development<br />
100% financing with loan of Rs. 1 crore for new businesses and Rs. 2 crores for businesses that are operational for 3 years with annual rate of interest 9.25 – 10%. Repayment tenure of 2 to 5 years with 3-6 months as a moratorium period.
<o:p></o:p></span></p>


<h3>• Cash Credit -</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
Working Capital<br />
Working Capital loan or Cash Credit depending on the business objectives and requirements
<o:p></o:p></span></p>


<h3>• SEF- Smile Equipment Finance</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
Minimum loan amount of Rs. 10 lakhs to procure new equipment with repayment period of 72 months
<o:p></o:p></span></p>


<h3>• SMILE -</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
SIDBI Make in India Soft Loan Fund for Micro Small & Medium Enterprises <br />
Minimum loan amount of Rs. 10 lakhs for buying new equipment and Rs. 25 lakhs for other financial needs. 10 years repayment period with moratorium period of 36 months.
<o:p></o:p></span></p>


<h3>• Loans under Partnership with OEM</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
Loan amount of up to Rs. 1 crore to acquire equipment from OEMs with repayment period of 60 months. Business must be operational for 3 years.
<o:p></o:p></span></p>

<h3>• STFS - SIDBI Trader Finance Scheme</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
Loan from Rs. 10 lakhs to Rs. 1 Crore with maximum repayment period of 60 months. Business must be operational for 3 years
<o:p></o:p></span></p>

<h2>Other types of financial services offered by SIDBI</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
•	TULIP - Top-up Loan for Immediate Purposes<br />
•	SAFE - SIDBI Assistance to Facilitate Emergency Response Against Coronavirus<br />
•	SAFE PLUS - SIDBI Assistance to Facilitate Emergency Response Against Coronavirus Plus<br />
•	SPEED PLUS - SIDBI – Loan for Purchase of Equipment for Enterprise’s Development PLUS<br />
•	STAR - SIDBI Term-loan Assistance for Rooftop Solar PV Plants <br />
•	RLS - SIDBI – Retail Loan Scheme for Trade Finance
<o:p></o:p></span></p>


<h2>Additional Loan Products</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
Start-up lifecycle loans through banks, NBFCs, SFBs etc.<br />
Alternate Investment Funds (AIFs) to support Funds of Funds for Start-ups<br />
Aspire Fund for new MSMEs that are into manufacturing and services<br />
India Aspiration Fund to attract equity-based investments in the MSME sector
<o:p></o:p></span></p>

<h2>Indirect Finance Options by SIDBI</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
SIDBI also provides financial support to Banks, SFBs and NBFCs. NBFCs that are registered with RBI lend loans to MSMEs. SIDBI also offers refinance schemes to build the equity base for banks and SFBs.
<o:p></o:p></span></p>


<h2>Micro-Lending Options by SIDBI</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
Micro Lending Development Department focusses on offering financial assistance to people and women who lack the monetary strength. <br />
Responsible Finance Initiatives are aimed at supporting banks and other financial institutions.<br />
Beyond Microfinance is a special scheme of loans that helps businesses to upgrade to a higher amount of loan at reasonable rates.
<o:p></o:p></span></p>

<h2>Steps to apply for a loan from SIDBI</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">	
STEP 1: Visit the official website of SIDBI – https://www.sidbi.in/or https://www.udyamimitra.in/<br />
STEP 2: Click the “Borrower’s corner” tab<br />
Step 3:  Fill in username and password<br />
STEP 4: Choose the scheme and enter the loan amount needed<br />
STEP 5: Fill in personal and business-related details and click on Register<br />
STEP 6: Complete the account verification process<br /><br />
After your account is activated, you can keep a track of your loan application process and submit the required documents. 
