			<section class="entry-header page-header text-center">
				<h1 class="entry-title title-font text-italic">
					MSMEs and the Credit Gap </h1>
							</section>
						<div class="entry-media post-thumbnail post-thumbnail-singular">
				<img src="images/Blog27MainPic.jpg" alt="Government Schemes That Can Benefit Your Business">
			</div>
			
			<div class="entry-content">
				<div class="page-content">
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">MSMEs make up around 30 per cent of India’s GDP.  <br><br>
However, there has been a large hurdle in assisting these MSMEs within the playfield of financial literacy, which has created a credit gap of as much as ₹16 lakh crore. So why is this happening? 
<o:p></o:p></span></p>

<h2>1)	Delay in Payments from Buyers pushing the deficit on working capital</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">A common problem among MSMEs are payment delays by buyers causing short-term credit gaps. These buyers being PSUs, and those within the Private Sector, effectively finance their own working capital by delaying payments to MSMEs, resulting in higher payables. As of December 2020, MSMEs were yet to receive more than 5 lakh crore as outstanding payment, according to the Union Minister of MSMEs.<o:p></o:p></span></p>


<h2>2)	The Credit Supply Deficit</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">While the estimated demand of credit for MSMEs lies at ₹105.49 lakh crore, formal resources only meet one-sixth of this demand at ₹16.94 lakh crore. After further calculation though, the ₹16.94 lakh crore gets divided into the various categories of MSMEs, which gives us 27% for micro (₹4.5 lakh crore), 40% for small (₹6.81 lakh crore), and 33% for medium (₹5.64 lakh crore) enterprises.
 <o:p></o:p></span></p>


<h2>3)	Lesser Lenders due to the Lockdown</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><i style="mso-bidi-font-style: normal;"> </i>Public Sector Banks (PSBs), Private Sector Banks (PVBs) and Non-Banking Financial Companies (NBFCs) have been caterers to the financial needs of MSMEs. However, due to the lockdown and the announcement regarding the Atmanirbhar Bharat stimulus package, NBFCs have been losing ground.<o:p></o:p></span></p>

<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">So how does one bridge the gap?<o:p></o:p></span></p>


<h3>1)	Credit Flow and NBFC promise</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">A  report by the RBI states that  as of June 2020, out of the total commercial lending exposure in India, the MSME sector only held for 25%. However the credit flow showed a year-on-year increase by 20%. One more reason to be optimistic is despite PSBs having held a strong position in the lending sector for MSMEs, the field is now seeing new NBFCs and PVBs as promising entrants. NBFCs are presumed to increase their hold again post-lockdown. <o:p></o:p></span></p>

<h3>2)	Invoice Financing</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Invoice financing is a short-term borrowing procedure from a lender or a bank. It treats pending invoices as collateral, covering approximately 75% of the total cost. This provides additional liquidity to businesses, assuring day-to-day activities are financeable, such as supply, raw materials, salary, etc. Such short-term liquidity assures a seamless flow without hurdles.<o:p></o:p></span></p>

<h3>3)	File ITR and GST Returns on Time</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">As more and more businesses start filing ITR and GST returns on time, it increases their credibility to resort to financial credit from banks and NBFCs. Filing each of these on a timely basis is essential to raise the credit score of the associated business. This helps shorten the gap between the businesses and its potential credit needs.  <o:p></o:p></span></p>


<h3>4)	Digitisation is the future</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">As more and more MSMEs now venture gradually into forming a digital footprint, even those in remote areas have gained increased financial access. This financial access not only covers quicker ways of business, but also provides platforms to file tax returns and also keeps MSMEs up-to-date with GST related updates. With such innovations catching up with the lending industry as well, MSMEs are becoming more financially aware by every passing day.  <o:p></o:p></span></p>


<h3>5)	Light Beyond the Tunnel</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Due to the disruption caused by the pandemic and nation-wide lockdown, it is safe to say that once we cross this stage and move back to our regular routine of work for the economy, the role of PSBs, PVBs and NBFCs will go way beyond regular financing. NBFCs, due to their innovative nature, have a high scope in building feasible yet quick solutions for the post-pandemic MSME sector. <o:p></o:p></span></p>


