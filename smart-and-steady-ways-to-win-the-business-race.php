				<h1 class="entry-title title-font text-italic">
					Smart and Steady Ways to Win the Business Race</h1>
							</section>
						<div class="entry-media post-thumbnail post-thumbnail-singular">
				<img src="images/Blog10MainPic.jpg" alt="Smart and Steady Ways to Win the Business Race">
