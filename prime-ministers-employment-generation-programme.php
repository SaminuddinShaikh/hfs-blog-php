				<h1 class="entry-title title-font text-italic">
					A complete Guide to Prime Ministers Employment Generation Programme (PMEGP)</h1>
							</section>
						<div class="entry-media post-thumbnail post-thumbnail-singular">
				<img src="images/Blog53MainPic.jpg" alt="Udyog Aadhar">
			</div>
			
			<div class="entry-content">
				<div class="page-content">
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
The Prime Ministers Employment Generation Programme (PMEGP) was established on March 31, 2008, by merging the preceding PMRY (Prime Minister Rozgar Yojana) and REGP programmes (Rural Employment Generation Programme). It was a move to combine two programmes with similar goals in order to focus on a single effort to encourage young people to start microbusinesses in the country's rural and urban areas. The administrative supervision of the programme is placed in the Ministry of MSME (Micro Small and Medium Enterprises), Government of India.
<br /><br />
The KVIC (Khadi and Village Industries Commission) at the national level, their directorates and boards at the state level, and the DIC at the district level are in charge of implementing the PMEGP. It is largely a credit-linked subsidy designed to increase employment among the country's youth by encouraging them to engage in manufacturing and service businesses by establishing new operations. It is critical to go deeper into the PMEGP scheme details in order to better understand the scheme's complexities.
<o:p></o:p></span></p>


<h2>Features of PMEGP Loan Scheme:</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
The PMEGP programme is based on providing subsidies for beginning a micro-enterprise to make the project sustainable by extending financing in the form of a PMEGP loan. The following are some of the distinguishing characteristics of this significant initiative:
<o:p></o:p></span></p>

<h2>Objectives</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">	
The fundamental goals of PMEGP KVIC are to integrate its four-fold goals, which include the following.<br />
•	Assist in the establishment of new micro-enterprise initiatives to increase job opportunities in both rural and urban areas.<br />
•	Bring together the country's artisans and unemployed youth in a cooperative effort to create self-employment opportunities.<br />
•	Prevent rural youngsters from migrating to cities in pursuit of long-term jobs by promoting traditional vocations all year.<br />
•	Provide self-employment opportunities for traditional artisans in rural areas in order to supplement their income.
<o:p></o:p></span></p>

<h2>Eligible of Loan</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
The PMEGP guidelines establish financial constraints in the following form, which are based on the general formula applicable to MSME:<br />
•	Maximum of Rs.25 lakhs for the manufacturer<br />
•	Maximum of Rs.10 lakhs for services
<o:p></o:p></span></p>

<h2>Self-Investment</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
This is decided by the location of the business.<br />
•	Plains: Maximum Rs.1 lakh<br />
•	Hills: Maximum Rs.1.5 lakhs
<o:p></o:p></span></p>

<h2>PMEGP loan details</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
For a complete picture of the subsidy awarded for PMEGP application, the fundamental parameter can be broken down as follows:<br />
•	SC/ST/OBC individuals, women, physically challenged people, ex-Defense Service Personnel, people from the North East, and those from the Border Areas are among the people who fall under this category.<br />
•	Banks give Term Loans to bridge the funding gap: <br />
People in the general category with a 10% self-share of project cost in rural areas will receive 25% Subsidy while urban areas will receive 15% Subsidy from the Government.<br />
People in the special category with a 5% self-share of project cost in rural areas will receive 35% Subsidy while urban areas will receive 25% Subsidy from the Government.
<o:p></o:p></span></p>

<h2>PMEGP Loan Tenure</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
 The Term loan is repayable over three to seven years. The banks have all agreed to prolong a 6-month freeze period.
 <o:p></o:p></span></p>
 
 <h2>Allocation Ratio</h2>
 <p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
The PMEGP loan is allocated proportionately in the following way. The subsidy is classified as margin money, and it is proportional to the borrower's capital expenditure.
<o:p></o:p></span></p>

<h2>PMEGP Loan Interest Rate</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
The bank's standard interest rate for MSME firms is 11 % to 12 % per annum. However, for a mix of Working and Fixed Capital expenses, the applicable interest rate is a concessionary 4% under the ISEC (Interest Subsidy Eligibility Certification) Scheme. The KVIC, under the budget heading "Grants," makes up the difference with the usual interest rate. Only personnel working in the production of Khadi and Polyvastra have access to this facility.
<o:p></o:p></span></p>

<h3>Eligibility Criteria for PMEGP Loan</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
Individuals and organizations who match the following criteria for PMEGP application are eligible for the loan:<br />
•	The individual must be at least 18 years old.<br />
•	Individuals must have passed the school's class VIII examination.<br />
•	Individual projects must meet the following criteria in terms of size:<br /><br />
Over Rs.10 lakhs, according to the manufacturer.<br />
Over Rs.5 lakhs in service.<br /><br />
•	Self-help organisations are eligible if they have not previously received any benefit from the scheme.<br />
•	Societies that have been registered.<br />
•	Co-operative societies in the producing business; and Charitable Trusts.<br />
•	There is no upper limit on how much money you can borrow.<br />
•	Only new businesses and those who have not previously benefited from comparable schemes are eligible for the credit option.
<o:p></o:p></span></p>

<h3>List of eligible activities</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
The loan is limited to only those activities that have been officially approved, excluding those on the negative list.
<o:p></o:p></span></p>

<h3>List of People Who Have Been Approved</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
•	Food processing is allowed as long as it is agro-based.<br />
•	Paper and fibre manufacture by hand.<br />
•	Forest-based, mineral-based, chemical-based, and polymer-based products.<br />
•	Rural engineering and biotechnology.<br />
•	Textile and service items
<o:p></o:p></span></p>

<h3>List Of People Who Are On The Negative List</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
•	Meat and slaughterhouse are on the negative list.<br />
•	Restaurants and Dhabas that serve alcohol.<br />
•	Tobacco and similar goods are prohibited.<br />
•	Toddy sales and tapping<br />
•	Cash crop-related activities.<br />
•	Horticulture, floriculture, and other related fields<br />
•	The list is merely intended to be illustrative and not exhaustive.
<o:p></o:p></span></p>

<h3>PMEGP Online Application Documents Required</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
Only the PMEGP site, which is a dedicated digital interface managed by KVIC, can be used to start the loan sanction procedure. The list of documents necessary is detailed, and it corresponds to the bank's requirements for a smooth transaction.<br />
•	All borrower KYC documents, such as Aadhaar, PAN, and EPIC, are required for information input into the PMEGP e portal.<br />
•	To fulfil the appropriately specified category, a caste or community certificate is required.<br />
•	A claim for the necessary subsidy.<br />
•	The cost of a project is the breakdown of a single cycle, which includes both capital expenditure and working capital components.<br />
•	A certificate from the bank's governing office stating that no operating capital is required.
<o:p></o:p></span></p>


<h3>Procedure for PMEGP application</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">
The borrower should to start the PMEGP online application process. There are three phases to the inputs needed to complete the procedure. Personal information is entered in the first phase. The second phase has the facility enter and all of the PMEGP status information, including the desired PMEGP Loan Limit.<br /><br />
It is also necessary to obtain the bank's account information. After this stage is completed, the application must be saved in order to proceed to the third phase, which is the submission of digitised documents. Only after all three phases have been completed can the application be submitted for evaluation. For future use, an application acknowledgment ID and password are sent to the registered email address.<br /><br />
