			<div class="entry-content">
				<div class="page-content">
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">As we grow up, from children to adults, along with the increasing frequency of responsibilities, there is also an increase in one question that we’re always asked more and more frequently as the years go by, 
<br><br>
“Where do you see yourself 5 years from now?” <br><br>

Despite being a question that is so popularly asked, one can’t deny the positive introspection that it brings along with it. <br><br>

Even in a business, a 5-year plan plays a very integral role to boost performances and achieve set goals and targets so the business can grow at an alarming rate, while being smart and steady as well. We did some research and came up with 5 things one must include in a 5-year plan for MSMEs that can bring about progressive growth for any micro, small, and medium enterprise. 
<o:p></o:p></span></p>

<h2>1)	Identify your approach</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">While the business industry is very vast, it also has many components that one should consider. Some businesses cater to a B2B approach which means catering to the needs of other businesses, while most MSMEs cater to a B2C approach which means catering directly to customers in general. Figure out which approach suits your business more, to plan your strategy accordingly. <o:p></o:p></span></p>

<h2>2)	Define your products and services</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">When you have a clear picture of your business and the commodities and services that the business has to offer, you can understand better about the viability and fluctuating demand for your offerings in the market. This will also help you determine the longevity of the demand. These factors play an integral role in making your business journey an efficient one.  <o:p></o:p></span></p>

<h2>3)	Determine your plan’s breadth</h2>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN"><i style="mso-bidi-font-style: normal;"> </i>It’s always good to understand the stretching potential or the flexibility of your plan to know which areas your industry caters to. For example, if your business is in the transport of groceries or vegetables, this will include having a strong/wide reach with vendors and suppliers of these commodities. Once you identify your industry, it will help you identify the various resources/vitals that you would need in your business journey.  <o:p></o:p></span></p>

<h3>4)	Set your objectives and goals</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">When an entrepreneur starts a journey, he/she always has some goals and aspirations in mind. Each MSME is founded on such goals and objectives. List down your short-term and long-term objectives/goals in your business plan. This will help you efficiently trace your journey towards achieving each of the goals, and identify resources such as investments and loans that will help you make the most of your journey of business growth.<o:p></o:p></span></p>


<h3>5)	Create financial goals</h3>
<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">Create benchmark values in managing the various finances of your business. To elaborate more, create financial projections or estimations for balance sheets, cash flow and bank statements. Review your financial goals on a bi-monthly/quarterly/semi-annual basis to keep track of your growth. This will help you understand the financial sustainability of your business and make better-informed and efficient financial decisions.<o:p></o:p></span></p>

<p class="MsoNormal" style="line-height: 150%;"><span style="font-size: 14.0pt; line-height: 150%; font-family: 'Poppins','sans-serif'; mso-fareast-font-family: Poppins; mso-bidi-font-family: Poppins;" lang="EN">A 5-year plan is often one that helps you keep a constant track of your business journey. It not only pushes your potential, but also affirms you of your business objectives and goals. By including these 5 must-haves, you can also increase the efficiency of your plan. 
<br><br>
Most of all though, a 5-year business plan with the above 5 points in mind, will help you see and reach where you want to be. 
