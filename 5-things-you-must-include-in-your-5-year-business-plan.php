				<h1 class="entry-title title-font text-italic">
					5 Things You Must Include in Your 5-Year Business Plan!</h1>
							</section>
						<div class="entry-media post-thumbnail post-thumbnail-singular">
				<img src="images/Blog22MainPic.jpg" alt="5 Things You Must Include in Your 5-Year Business Plan!">
